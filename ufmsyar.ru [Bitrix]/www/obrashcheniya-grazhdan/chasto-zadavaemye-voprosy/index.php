<p>
	<strong><span style="color: #993300;">Вопрос: Возможно ли заменить паспорт гражданина РФ гражданину, имеющему регистрацию по месту пребывания на территории Ярославской области, если у него имеется регистрация по месту жительства в Алтайском крае?</span></strong>
