<h2><span style="color: #993300;"><strong>Паспорт гражданина Российской Федерации</strong></span></h2>
<p>
	<span style="color: #993300;"><strong>Вопрос: Какой штраф в случае утери паспорта гражданина России необходимо заплатить? Какие документы необходимы для его восстановления? </strong></span>
</p>
<p align="justify">
	 В соответствии со ст. 19.16 КоАП РФ утрата паспорта влечет предупреждение или наложение административного штрафа в размере от 1 до 3 минимальных размеров оплаты труда. При утрате паспорта гражданин предоставляет письменное заявление, в котором указывается, где, когда, и при каких обстоятельствах был утрачен паспорт, заявление о выдаче (замене) паспорта по форме № 1П, 4 личные фотографии установленного образца, документы, необходимые для проставления отметок в паспорте (военный билет, свидетельства о рождении детей до 14 лет, документы, подтверждающие регистрацию по месту <br>
	 жительства, свидетельство о регистрации брака, свидетельство о расторжении брака), а также квитанцию об уплате государственной пошлины.
</p>
<p>
	<strong><span style="color: #993300;">Вопрос: У моей дочери в свидетельстве о рождении написано имя Алёна (через Ё), а в паспорте, выданном в 14 лет записано Алена (через Е). Каким образом внести изменения в паспорт, либо его заменить?</span></strong>
</p>
<p>
</p>
<p>
	 В случае разночтений записей, произведенных в свидетельстве о рождении и в паспорте, паспорт подлежит замене.
</p>
<p>
	 По вопросу замены паспорта Ваша дочь вправе обратиться в структурное подразделение ФМС России по месту жительства, месту пребывания или по месту обращения, предоставив  в Вашем случае пакет документов:
</p>
<p>
	 1.Заявление о выдаче (замене) паспорта формы № 1П;
</p>
<p>
	 2.Две личные фотографии установленного образца;
</p>
<p>
	 3.Паспорт, подлежащий замене;
</p>
<p>
	 4.Свидетельство о рождении (документ, подтверждающий основание замены паспорта).
</p>
<p>
	 5.Квитанция об оплате госпошлины за бланк паспорта.
</p>
<p align="justify">
	<span style="color: #993300;"><strong>Вопрос: Согласно Административному регламенту по предоставлению государственной услуги по выдаче, замене и по исполнению государственной функции по учету паспортов гражданина Российской Федерации, удостоверяющих личность гражданина Российской Федерации на территории Российской Федерации основанием для замены паспорта может являться изменение внешности. Существуют ли критерии оценки изменения внешности? Может ли быть отказано гражданину в замене паспорта по данному основанию?</strong></span>
</p>
<br>
<p>
</p>
<p align="justify">
	 Действительно,  в соответствии с п. 7  Административного регламента ФМС по предоставлению государственной услуги по выдаче, замене и использованию государственной функции по учету паспортов гражданина Российской Федерации, удостоверяющих личность гражданина Российской Федерации на территории Российской Федерации от 28.12.2006 года № 1105, предусмотрено получение  паспорта гражданина Российской Федерации в случае изменения внешности. Однако критерии  оценки изменения внешности законодательством не прописаны.
</p>
<p>
	<strong><span style="color: #993300;">Вопрос: Хочу поменять паспорт, в связи с достижением 20-летнего возраста. С чего начать и куда мне обратиться? Проживаю в Заволжском районе г. Ярославля</span></strong>
