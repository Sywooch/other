</p>
<p>
	<span style="color: #993300;"><strong>Вопрос: Имею задолженность, может ли быть ограничен выезд за границу?</strong></span>
</p>
<p align="justify">
	 В соответствии с п. 5 статьи 15 Федерального Закона от 15.08.1996 № 114-ФЗ «О порядке выезда из Российской Федерации и въезда в Российскую Федерацию» право гражданина Российской Федерации на выезд из Российской Федерации может быть ограничено в случаях, если он уклоняется от исполнения обязательств, наложенных на него судом, до исполнения обязательств либо до достижения согласия сторонами. Постановление о временном ограничении выезда из Российской Федерации выносит Управление Федеральной службы судебных приставов. Информацию о наложеных судебными приставами ограничениях на выезд за границу можно узнать в районном отделе судебных приставов по месту вашей регистрации.
</p>
<p align="justify">
	<span style="color: #993300;"><strong>Вопрос: Требуется ли дополнительная вклейка фотографии ребенка в ранее выданный действительный заграничный паспорт старого образца с уже имеющимися сведениями о ребенке и вносятся ли сведения о детях в заграничный паспорт нового поколения?</strong></span>
</p>
<p align="justify">
	 Паспорта, оформленные ранее действительны для пересечения Государственной границы. С 21 марта 2009 года при внесении сведений о детях в заграничные паспорта старого образца фотографии детей вклеиваются с момента рождения до достижения ребенком 14-летнего возраста.<strong>В заграничный паспорт нового поколения сведения о детях не вносятся, ребенку оформляется отдельный заграничный паспорт.</strong>
</p>
<h4><span style="color: #993300;">Вопрос:  может ли мой бывший супруг запретить выезжать за границу нашим детям и какова процедура.</span></h4>
<p>
	 В соответствии с требованиями постановления Правительства Российской Федерации от 12.05.2003 г. № 273 «Об утверждении Правил подачи заявления о несогласии на выезд из Российской Федерации несовершеннолетнего гражданина Российской Федерации» Ваш бывший супруг вправе ввести временные ограничения на выезд из Российской Федерации Ваших несовершеннолетних детей. Для этого должен обратиться в УФМС России по Ярославской области с заявлением о введении временных ограничений на выезд детям. В течении трех рабочих дней с момента обращения заявителя вводятся временные ограничения и информируются все заинтересованные службы и мать детей.
</p>
<p>
	<span style="color: #993300;"><strong>Вопрос: В течение какого времени действительны заявление на заграничный паспорт нового образца и квитанция об оплате госпошлины?</strong></span>
</p>
<p>
	 Заявление действительно в течение 1 месяца, квитанция - в течение 3-х лет.
