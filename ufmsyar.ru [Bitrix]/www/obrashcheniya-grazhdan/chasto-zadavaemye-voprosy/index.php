<h2 align="justify"><strong><span style="color: #993300;">Иностранным гражданам</span></strong></h2>
<p align="justify">
	<strong><span style="color: #993300;">Вопрос: Необходимо ли личное присутсвие иностранного гражданина при подаче документов на квоту для временного проживания?</span></strong>
</p>
<p align="justify">
	 При наличии невозможности личной явки иностранный гражданин вправе обратиться с пиьменным заявлением о выделении квоты для получения разрешения на временное проживание с приложением документов, подтверждающих обстоятельства, в соответствии с которыми он считает возможным выделение ему квоты во внеочередном порядке
</p>
<p align="justify">
	<strong style="color: #993300;">Вопрос: Имеет ли право иностранный гражданин самостоятельно оформить разрешение на работу, если он пока не нашел работодателя?</strong>
</p>
<p align="justify">
	 Да, но срок действия разрешения на работу иностранному гражданину, не заключившему трудовой договор, будет выдаваться на срок не более 90 суток с даты въезда в Российскую Федерацию.
</p>
<p align="justify">
