<p align="justify">
	<span style="color: #993300;"><strong>Вопрос: Бывают ли случаи, когда заграничные паспорта остаются неполученными? Что происходит с такими документами?</strong></span>
</p>
<p>
	 К сожалению, некоторые граждане, обратившиеся в УФМС России по Ярославской области и его структурные подразделения с заявлениями о выдаче заграничных паспортов, впоследствии не приходят за уже оформленными документами в силу различных обстоятельств. Например - изменились планы на отпуск и паспорт уже не так нужен; длительная командировка или болезнь. Такие паспорта хранятся в миграционной службе в течение срока их действия, т.е. 5 лет, а потом уничтожаются в установленном порядке как невостребованные.<br>
	 Конечно, гражданин может получить свой документ не только в тот день, когда ему было предложено сотрудниками миграционной службы, но и гораздо позже. При этом нужно предварительно позвонить в то подразделение миграционной службы, в которое Вы подавали заявление о выдаче паспорта и уточнить новую дату получения документа. Эта необходимость вызвана тем, что сотрудники должны будут сделать ряд повторных проверок.<br>
	 УФМС России по Ярославской области убедительно просит жителей г. Ярославля и Ярославской области своевременно получать заграничные паспорта и хранить их у себя, чтобы иметь возможность использовать в любое удобное для себя время.
</p>
<p>
	<span style="color: #993300;"><strong>Вопрос: В настоящий момент я занимаюсь оформлением загранпаспорта нового поколения. У меня в старом загранпаспорте, срок действия которого заканчивается через месяц, есть визы стран Шенгенского соглашения, а также других стран, которые имеют значение при оформлении виз в будущем, так как многие посольства требуют сведения о ранее имевшихся заграничных паспортах. Почему при выдаче нового загранпаспорта у меня изымают старый?</strong></span>
</p>
<p>
