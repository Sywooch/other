<p>
	 Подача заявления для оформления документов через Интернет – специфический процесс, где существуют свои нюансы, незнание которых иногда приводит к отклонению&nbsp;&nbsp;заявления гражданина. В этой статье даны рекомендации, которые помогут избежать ошибок при заполнении заявления.
</p>
<p>
	<strong>1. Выдача (замена) паспорта гражданина Российской Федерации.</strong>
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При заполнении анкеты данные о месте рождения гражданина должны быть полными, обязательно с указанием района или области, с указанием признака населенного пункта (город, село и т.д.). Сведения о месте рождения должны быть идентичны тем, что указаны в паспорте гражданина РФ, либо в свидетельстве о рождении ребенка.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Граждане, проживающие в многоквартирном доме, должны не забывать указывать в заявлении не только номер дома, но и номер квартиры.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При заполнении заявления важно, чтобы дата замены паспорта, указанная в заявлении, обязательно совпадала с датой его подачи.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Довольно часто заявления отклоняются из-за несоответствия загружаемых фотографий необходимым требованиям. Фото должны иметь расширение JPEG, максимальный «вес» в 300 КБ, разрешение в 600 dpi и размер 35x45 мм. На фотографиях не допускается эффект «красных глаз», ретушь, фон должен быть одноцветным, светлым (но не белым). Изображение заявителя должно соответствовать его современному внешнему виду. Головные уборы, темные очки или т. п. не допускаются (за исключением случаев, когда ношение головных уборов диктуется религиозными убеждениями, а очки носят постоянно). Запечатленный на снимке человек должен быть серьезным, смотреть прямо перед собой. Положение тела должно быть прямым. Данные требования распространяются и на фотографии, загружаемые при подаче документов для оформления заграничного паспорта.
</p>
<p>
	<strong>2. Оформление заграничного паспорта</strong>
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Сведения о трудовой деятельности гражданина должны быть указаны обязательно за последние 10 лет. Так, например, если подача документов на заграничный паспорт осуществляется в октябре 2011 года, датой, с которой необходимо начинать заполнять п. 14 анкеты, будет 10.2001. К трудовой деятельности относятся также учеба и военная служба. Военнослужащие среди прочих сведений о своей трудовой деятельности указывают номер военной части, вид и род войск, воинскую специальность. Перерывы в трудовой деятельности не должны превышать 3-х месяцев, в противном случае такие интервалы указываются в отдельной строке. Информация о трудовой деятельности, указанная в анкете, должна совпадать с той, что содержится в трудовой книжке гражданина.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Все, кто имеет допуск к государственной тайне, должны указывать номер формы допуска.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Данные о месте рождения гражданина, адресе его регистрации, опять же должны быть идентичны тем, что указаны в паспорте гражданина РФ, либо в свидетельстве о рождении ребенка.
</p>
<p>
	<strong>3. Предоставление адресно-справочной информации</strong>
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Заявления граждан часто отклоняются из-за недостаточности сведений о лице, в отношении которого осуществляется запрос: нередко не указаны даже фамилия, имя и отчество.
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Также гражданину необходимо обязательно указывать свои контактные данные, чтобы с ним можно было связаться для получения дополнительных сведений, без которых невозможно выполнить запрос.
</p>
<p>
	<strong>4. Регистрационный учет граждан по месту пребывания, регистрационный учет граждан по месту жительства, снятие граждан с регистрационного учета по месту жительства.</strong>
</p>
<p>
	 ·&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При получении госуслуги через Портал по данным направлениям деятельности УФМС России по Ярославской области необходимо правильно указывать документы, на основании которых происходит вселение в жилое помещение. Тонкость в том, что если собственник желает поселить в жилое помещение себя, то таким документом будет Свидетельство о собственности. Если же собственник помещение и вселяемый в это помещение – разные люди, то документом, на основании которого происходит вселение, будет заявление собственника жилого помещения о вселении туда стороннего лица.
</p>
