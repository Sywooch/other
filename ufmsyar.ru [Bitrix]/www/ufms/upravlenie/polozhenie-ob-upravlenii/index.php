<h2 align="center">Об УФМС России по Ярославской области</h2>
<p>
	 В соответствии с Указом Президента Российской Федерации от 19 июля 2004 года № 928 «Вопросы Федеральной миграционной службы» приказом ФМС России от 2 декабря 2005 года № 213 образован территориальный орган Федеральной Миграционной службы - Управление Федеральной миграционной службы по Ярославской области (УФМС России по Ярославской области). <br>
	 Территориальный орган создан для осуществления правоприменительных функций, функций по контролю, надзору и оказанию государственных услуг в сфере миграции, а также для реализации отдельных установленных законодательными и иными нормативными правовыми актами РФ задач и функций Федеральной миграционной службы России. <br>
	 Территориальный орган является государственным органом, находящимся в подчинении ФМС России, в гражданских правоотношениях участвует в форме государственного учреждения. Контроль за деятельностью территориального органа осуществляет ФМС России.<br>
	 Территориальный орган в своей деятельности руководствуется Конституцией Российской Федерации, Федеральными конституционными законами, федеральными законами, актами Президента Российской Федерации и Правительства Российской Федерации, общепризнанными принципами и нормами международного права, международными договорами Российской Федерации, нормативными правовыми актами Министерства внутренних дел РФ, приказами и распоряжениями ФМС России.<br>
	 Деятельность территориального органа осуществляется в соответствии с принципами уважения прав и свобод человека и гражданина, законности и гуманизма, на основе планирования, сочетания единоначалия в решении вопросов служебной деятельности и коллегиальности при их обсуждении.
</p>
<p>
	<strong>Основными задачами территориального органа являются: </strong><br>
	 • производство по делам о гражданстве РФ, оформление и выдача основных документов, удостоверяющих личность гражданина РФ;<br>
	 • осуществление регистрационного учета граждан РФ по месту пребывания и по месту жительства в пределах РФ и контроля за соблюдением гражданами и должностными лицами правил регистрации и снятия с регистрационного учета граждан РФ;<br>
	 • оформление и выдача иностранным гражданам и лицам без гражданства документов для въезда в РФ, проживания и временного пребывания в РФ;<br>
	 • осуществление контроля за соблюдением иностранными гражданами и лицами без гражданства установленных правил проживания и временного пребывания в РФ;<br>
	 • реализация во взаимодействии с другими территориальными органами федеральных органов исполнительной власти мер по предупреждению и пресечению незаконной миграции;<br>
	 • исполнение законодательства РФ по вопросам беженцев и вынужденных переселенцев, участие в установленном порядке в предоставлении политического убежища иностранным гражданам и лицам без гражданства;<br>
	 • осуществление в соответствии с законодательством РФ контроля и надзора в сфере внешней трудовой миграции, привлечения иностранных работников в РФ и трудоустройства граждан РФ за пределами РФ.
</p>
<p>
	<strong>Управление Федеральной миграционной службы по Ярославской области и его структурные подразделения осуществляют в соответствии с законодательством Российской Федерации: </strong><br>
	 • оформление и выдачу работодателям, заказчикам работ (услуг) разрешений на привлечение в РФ и использование иностранных работников и разрешений на работу иностранным гражданам и лицам без гражданства;<br>
	 • оформление и выдачу иностранным гражданам патентов;<br>
	 • выдачу иностранным гражданам и лицам без гражданства виз на въезд в РФ, их продление, аннулирование и восстановление;<br>
	 • выдачу иностранным гражданам и лицам без гражданства разрешения на временное проживание в РФ;<br>
	 • выдачу иностранным гражданам и лицам без гражданства вида на жительство в РФ;<br>
	 • принятие от лиц, проживающих на территории РФ, заявлений по вопросам гражданства РФ;<br>
	 • составление заключений на заявления об изменении гражданства РФ в общем порядке и направление их в ФМС России;<br>
	 • исполнение решений по заявлениям об изменении гражданства РФ;<br>
	 • ведение учета лиц, в отношении которых территориальным органом приняты решения об изменении гражданства;<br>
	 • отмену принятых территориальным органом решений по вопросам гражданства РФ;<br>
	 • выдачу и замену гражданам РФ документов, удостоверяющих их личность;<br>
	 • регистрационный учет граждан РФ по месту пребывания и по месту жительства в пределах РФ, а также регистрацию иностранных граждан и лиц без гражданства, временно пребывающих и проживающих на территории РФ;<br>
	 • оформление и выдачу паспортов и иных документов гражданам РФ для выезда из РФ и въезда в РФ;<br>
	 • учет выданных паспортов гражданина РФ;<br>
	 • оформление и выдачу приглашений иностранным гражданам и лицам без гражданства на въезд в РФ, а также ведение учета оформляющих приглашения юридических лиц;<br>
	 • реализацию мер по предупреждению и пресечению незаконной миграции, иммиграционный контроль в отношении иностранных граждан и лиц без гражданства;<br>
	 • проставление отметки о запрещении въезда в РФ в документе, удостоверяющем личность иностранного гражданина или лица без гражданства;<br>
	 • проведение государственной дактилоскопической регистрации;<br>
	 • учет, регистрацию, прием и временное размещение лиц, ходатайствующих о предоставлении убежища либо о признании их беженцами, получивших статус беженца, а также оказание содействия беженцам во временном обустройстве на территории осуществления полномочий;<br>
	 • учет, регистрацию, прием и временное размещение лиц, ходатайствующих о признании их вынужденными переселенцами, получивших статус вынужденного переселенца, а также оказание содействия вынужденным переселенцам в обустройстве на новом месте жительства на территории осуществления полномочий;<br>
	 • проведение идентификации иностранных граждан и лиц без гражданства, ходатайствующих о признании их беженцами и находящихся на территории осуществления полномочий;<br>
	 • производство по делам об административных правонарушениях в пределах установленной компетенции;<br>
	 • представление в установленном порядке в судах интересов Президента РФ, Правительства РФ, МВД России и ФМС России по вопросам своей компетенции, а также защиту в судах интересов территориального органа;<br>
	 • подготовку и предоставление в установленном порядке и в установленные сроки ведомственной и государственной статистической отчетности.
</p>
<p>
	<strong>Управление Федеральной миграционной службы по Ярославской области принимает в соответствии с законодательством Российской Федерации решения: </strong><br>
	 • по заявлениям об изменении гражданства РФ;<br>
	 • о продлении либо сокращении срока временного пребывания иностранных граждан или лиц без гражданства в Российской Федерации;<br>
	 • о выдаче иностранным гражданам или лицам без гражданства разрешений на временное проживание в РФ или вида на жительство, а также об аннулировании указанных разрешений или вида на жительство;<br>
	 • о выдаче, приостановлении срока действия или аннулировании разрешений на привлечение и использование иностранных работников, разрешений иностранным гражданам или лицам без гражданства на работу, лицензий на осуществление деятельности, связанной с трудоустройством граждан РФ за пределами РФ.<br>
	 • Подготавливает и исполняет в соответствии с законодательством РФ решения о депортации иностранных граждан и лиц без гражданства.<br>
	 • Осуществляет в установленном порядке мероприятия по административному выдворению за пределы РФ иностранных граждан и лиц без гражданства.<br>
	 • Оформляет и направляет в ФМС России в установленном порядке материалы, подтверждающие обстоятельства, являющиеся основанием для принятия решения о нежелательности пребывания (проживания) иностранных граждан или лиц без гражданства в РФ.<br>
	 • Осуществляет в пределах своей компетенции меры по недопущению въезда в РФ иностранных граждан или лиц без гражданства по основаниям, предусмотренным законодательством РФ.<br>
	 • Участвует в установленном порядке в осуществлении лицензирования деятельности, связанной с трудоустройством граждан РФ за пределами РФ.<br>
	 • Участвует в реализации государственной политики РФ в отношении соотечественников за рубежом на территории осуществления полномочий.<br>
	 • Ведет адресно-справочную работу на территории осуществления полномочий.<br>
	 • Проводит прием граждан, своевременно и полно рассматривает устные и письменные обращения граждан, принимает по ним соответствующие решения и направляет ответы в установленный законодательством РФ срок.<br>
	 • Принимает в установленном порядке участие в подготовке предложений по установлению субъекту РФ квот на выдачу иностранным гражданам и лицам без гражданства приглашений на въезд в РФ в целях осуществления трудовой деятельности и разрешений на временное проживание в РФ.<br>
	 • Взаимодействует и проводит в установленном порядке консультации с российскими и зарубежными общественными организациями, правоохранительными органами иностранных государств по вопросам, отнесенным к компетенции территориального органа.
</p>
<p>
	 Территориальный орган является юридическим лицом, имеет обособленное имущество, самостоятельный баланс, печать с изображением Государственного герба Российской Федерации и своим наименованием, а также соответствующие бланки, печати и штампы.
</p>
