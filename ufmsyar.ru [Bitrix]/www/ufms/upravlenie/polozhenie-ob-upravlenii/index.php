</p>
<p>
	 15.7. Оформляет и направляет в ФМС России в установленном порядке материалы, подтверждающие обстоятельства, являющиеся основанием для принятия решения о нежелательности пребывания (проживания) иностранных граждан или лиц без гражданства в Российской Федерации.
</p>
<p>
	 15.8. Осуществляет координацию деятельности органов исполнительной власти субъекта Российской Федерации по оказанию содействия добровольному переселению в Российскую Федерацию соотечественников, проживающих за рубежом.
</p>
<p>
	 15.9. Участвует в установленном порядке в осуществлении лицензирования деятельности, связанной с трудоустройством граждан Российской Федерации за пределами Российской Федерации.
</p>
<p>
	 15.10. Участвует в реализации государственной политики Российской Федерации в отношении соотечественников за рубежом.
</p>
<p>
	 15.11. Ведет адресно-справочную работу.
</p>
<p>
	 15.12. Проводит прием граждан, своевременно и полно рассматривает устные и письменные обращения граждан, принимает по ним соответствующие решения и направляет ответы в установленный законодательством Российской Федерации срок.
</p>
<p>
	 15.13. Принимает в установленном порядке участие в подготовке предложений по установлению субъекту Российской Федерации квот на выдачу иностранным гражданам и лицам без гражданства приглашений на въезд в Российскую Федерацию в целях осуществления трудовой деятельности и разрешений на временное проживание в Российской Федерации.
</p>
<p>
	 15.14. Принимает меры по укреплению и развитию материально-технической базы территориального органа.
</p>
<p>
	 15.15. Принимает участие в формировании федеральных баз данных в установленной сфере деятельности, несет ответственность за полноту и своевременность внесения в них сведений.
</p>
<p>
	 15.16. Взаимодействует и проводит в установленном порядке консультации:
</p>
<p>
	 с российскими и зарубежными общественными организациями по вопросам, отнесенным к компетенции территориального органа;
</p>
<p>
	 с компетентными в области миграции органами иностранных государств в случаях, когда такой порядок обмена информацией предусмотрен международными договорами Российской Федерации межведомственного характера, заключенными между ФМС России и указанными органами иностранного государства, а также в соответствии с поручением руководства ФМС России.
</p>
<p>
	 15.17. Обобщает практику применения законодательства Российской Федерации в установленной сфере деятельности и вносит руководству ФМС России предложения по его совершенствованию.
</p>
<p>
	 15.18. Осуществляет в установленном законодательством Российской Федерации порядке деятельность, связанную с размещением заказов, и заключает государственные контракты на поставку товаров, выполнение работ, оказание услуг, проведение научно-исследовательских работ для нужд территориального органа.
</p>
<p>
	 15.19. Осуществляет функции распорядителя и получателя средств федерального бюджета в части средств, предусмотренных на содержание территориального органа и реализацию возложенных на него функций.
</p>
<p>
	 15.20. Осуществляет бюджетные полномочия администратора доходов бюджетов бюджетной системы Российской Федерации.
</p>
<p>
	          15.21. Осуществляет защиту сведений, составляющих государственную и иную охраняемую законом тайну, и меры, связанные с допуском сотрудников, гражданских служащих и работников территориального органа к сведениям, составляющим государственную и иную охраняемую законом тайну.
</p>
<p>
	 15.22. Принимает участие в установленном законодательством Российской Федерации порядке в обеспечении безопасности лиц, подлежащих государственной защите.
</p>
<p>
	 15.23. Осуществляет мероприятия в соответствии с законодательством Российской Федерации по мобилизационной подготовке, мобилизации и гражданской обороне.
</p>
<p>
	 15.24. Обеспечивает по запросам территориального органа федерального органа исполнительной власти, осуществляющим функции по выработке и реализации государственной политики в сфере внутренних дел информирование о лицах, в отношении которых законодательством Российской Федерации предусмотрено установление и осуществление административного надзора.
</p>
<p>
	 15.25. Осуществляет иные полномочия в установленной сфере деятельности, если такие полномочия предусмотрены федеральными законами, актами Президента Российской Федерации и Правительства Российской Федерации, Положением о Федеральной миграционной службе, утвержденным постановлением Правительства Российской Федерации от 13 июля 2012 г. № 711, нормативными и правовыми актами ФМС России.
</p>
<p align="center">
	  <strong>III</strong><strong>. Права Управления</strong>
</p>
<p>
	<strong> </strong>16. Управление для осуществления своих полномочий имеет право:
</p>
<p>
	 16.1. Запрашивать и получать в установленном порядке от территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации, органов местного самоуправления и иных организаций документы, справочные материалы и другую информацию, необходимые для принятия решений по <em>­</em>вопросам установленной сферы деятельности Управления.
</p>
<p>
	 16.2. Подготавливать и направлять в органы государственной власти субъекта Российской Федерации, в органы местного самоуправления, а также в иные организации информацию о недостатках, выявленных при про ведении мероприятий по контролю (надзору) за соблюдением миграционного законодательства, вносить соответствующие предложения по их устранению.
</p>
<p>
	 16.3. Привлекать в установленном порядке для выработки решений по вопросам, отнесенным к установленной сфере деятельности, научные и иные организации, ученых и специалистов, в том числе на договорной основе.
</p>
<p>
	 16.4. Участвовать в разработке и рассмотрении концепций, программ, инициатив общественных объединений и граждан по наиболее актуальным вопросам деятельности.
</p>
<p>
	 16.5. Заключать гражданско-правовые договоры.
</p>
<p>
	 16.6. Выступать в качестве заказчика по капитальному строительству, реконструкции, капитальному и текущему ремонту объектов территориального органа.
</p>
<p>
	 16.7. Участвовать в установленном порядке в информационном обмене.
</p>
<p>
	 16.8. Формировать и вести в соответствии с законодательством Российской Федерации учеты, информационные. банки (базы) данных справочной, статистической, правовой и иной информации, а также пользоваться в установленном порядке федеральными учетами, банками (базами) данных в этой области других федеральных органов исполнительной власти.
</p>
<p>
	 16.9. Взаимодействовать в соответствии с международными договорами и законодательством Российской Федерации и в порядке, установленном ФМС России, с компетентными органами иностранных государств и международными организациями в установленной сфере деятельности.
</p>
<p>
	 16.10. Взаимодействовать в установленном порядке со средствами массовой информации по направлениям деятельности территориального органа.
</p>
<p>
	 16.11. Осуществлять по заявкам редакций средств массовой информации аккредитацию журналистов для освещения деятельности территориального органа.
</p>
<p>
	 16.12. Образовывать координационные, консультативные, экспертные и совещательные органы (советы, комиссии), в том числе межведомственные, для рассмотрения вопросов в установленной сфере деятельности.
</p>
<p>
	 16.13. Использовать в своей деятельности информационные системы, системы связи и передачи данных, а также современную информационно-телекоммуникационную инфраструктуру.
</p>
<p>
	 16.14. Применять в порядке, установленном законодательством Российской Федерации, электронные формы приема и регистрации документов, уведомления о ходе оказания государственных услуг, а также электронные формы взаимодействия с территориальными органами федеральных органов исполнительной власти, расположенными на территории осуществления полномочий, органами исполнительной власти субъекта Российской Федерации, иными государственными органами, органами местного самоуправления, общественными объединениями и организациями.
</p>
<p>
	 16.15. Использовать достижения в области науки и. техники, современные технологии и информационные системы.
</p>
<p align="center">
	  <strong>IV. Организация деятельности Управления</strong>
</p>
<p>
	<strong> </strong>         17. Управление возглавляет начальник (руководитель), назначаемый на должность и освобождаемый от должности в установленном порядке.
</p>
<p>
	 18. Начальник (руководитель) Управления имеет заместителей, назначаемых на должность и освобождаемых от должности в установленном порядке.
</p>
<p>
	 19. Начальник (руководитель) Управления:
</p>
<p>
	 19.1. Осуществляет руководство деятельностью Управления, несет персональную ответственность за надлежащее выполнение возложенных на территориальный орган задач и функций, реализацию предоставленных прав.
</p>
<p>
	 19.2. Распределяет функциональные обязанности между своими заместителями, осуществляет контроль за их исполнением, делегирует в установленном порядке, исходя из интересов территориального органа, часть своих полномочий заместителям.
</p>
<p>
	 19.3. Издает в пределах своей компетенции приказы и распоряжения по вопросам организации деятельности территориального органа, обязательные для выполнения всем личным составом территориального органа.
</p>
<p>
	 19.4. Устанавливает в соответствии с законодательством Российской Федерации служебный (внутренний) распорядок и режим работы Управления.
</p>
<p>
	 19.5. Вносит:
</p>
<p>
	 19.5.1. Руководителю ФМС России:
</p>
<p>
	 1) проект положения о территориальном органе;
</p>
<p>
	 2) предложения по структуре и штатному расписанию территориального органа, фонду оплаты труда сотрудников, гражданских служащих и работников территориального органа.
</p>
<p>
	 19.5.2. Начальнику территориального органа ФМС России на окружном уровне - предложения о назначении на должность, освобождении от должности заместителей руководителя территориального органа.
</p>
<p>
	 19.6. Направляет в установленном порядке сотрудников, гражданских служащих и работников Управления в служебные командировки.
</p>
<p>
	 19.7. Формирует в пределах полномочий кадровый резерв для замещения должностей в Управлении.
</p>
<p>
	 19.8. Устанавливает и изменяет, в пределах своей компетенции и средств, выделенных на денежное довольствие и заработную плату, должностные оклады в размерах, определенных по соответствующим должностям, надбавки и выплаты сотрудникам, гражданским служащим и работникам Управления.
</p>
<p>
	 19.9. Организует и обеспечивает подбор, расстановку и профессиональную подготовку кадров в Управления, соблюдение ими служебной дисциплины, в установленном порядке направляет сотрудников, гражданских служащих и работников на переподготовку, повышение квалификации и стажировку.
</p>
<p>
	 19.10. Осуществляет в пределах своей компетенции прием, назначение, перемещение, временное отстранение, освобождение от должности и увольнение сотрудников, гражданских служащих и работников Управления.
</p>
<p>
	 19.11. Утверждает положения о структурных подразделениях Управления, должностные регламенты (должностные инструкции) сотрудников, гражданских служащих и работников Управления.
</p>
<p>
	 19.12. По поручению руководителя Службы представляет ФМС России во взаимоотношениях с территориальными органами федеральных органов исполнительной власти, с органами государственной власти субъектов Российской Федерации, органами местного самоуправления, общественными объединениями и иными организациями.
</p>
<p>
	 19.13. Организует в пределах своей компетенции проведение аттестации, квалификационных. экзаменов в отношении сотрудников, гражданских служащих и работников Управления.
</p>
<p>
	 19.14. Присваивает классные чины младшей, старшей и ведущей групп должностей гражданским служащим Управления. Вносит руководителю ФМС России предложения о присвоении специальных званий сотрудникам Управления, а также классных чинов главной и ведущей группы должностей гражданским служащим Управления в соответствии с номенклатурой ФМС России.
</p>
<p>
	 19.15. Применяет в пределах своей компетенции меры поощрения и взыскания в отношении сотрудников, гражданских служащих и работников Управления. Вносит руководителю ФМС России представления к награждению государственными и ведомственными наградами отличившихся сотрудников, гражданских служащих и работников Управления.
</p>
<p>
	 19.16. Предоставляет в пределах своей компетенции отпуска сотрудникам, гражданским служащим и работникам Управления.
</p>
<p>
	 1.9.17. Обладает полномочиями представителя нанимателя от имени Российской Федерации, предоставляемыми руководителем Службы, в отношении гражданских служащих Управления.         
</p>
<p>
	 19.18. Решает в пределах своей компетенции в соответствии с законодательством Российской Федерации иные вопросы, связанные с прохождением службы (трудовой деятельностью) сотрудников, гражданских служащих и работников Управления.
</p>
<p>
	 19.19. Распоряжается в соответствии с законодательством Российской Федерации денежными и материально-техническими средствами Управления в пределах утвержденной бюджетной сметы, осуществляет контроль за их использованием.
</p>
<p>
	 19.20. Представляет на утверждение руководителю ФМС России проект сметы доходов и расходов Управления в пределах установленных ассигнований, несет персональную ответственность за целевое использование бюджетных финансовых средств, организацию бюджетного учета.
</p>
<p>
	 49.21. Утверждает положение о коллегии Управления и состав коллегии Управления.
</p>
<p>
	 19.22. Утверждает положение о совещании при начальнике (руководителе) Управления и состав совещания при начальнике (руководителе) Управления.
</p>
<p>
	 19.23. Утверждает ежегодный план основных организационных мероприятий Управления и отчет о его исполнении.
</p>
<p>
	 19.24. Издает в пределах своей компетенции приказы и распоряжения по вопросам организации деятельности территориального органа, обязательные для выполнения всем личным составом территориального органа.
</p>
<p>
	 19.25. Осуществляет другие полномочия в соответствии с законодательством Российской Федерации.
</p>
<p>
	 20. Начальник Управления находится в оперативном подчинении начальника УФМС России по г. Москве.
</p>
<p>
	 21. В Управлении образуется коллегия в составе начальника (руководителя) Управления (председатель коллегии), его заместителей, входящих в нее по должности, а также других лиц.
</p>
<p>
	 22. Делопроизводство, включая комплектование, хранение, учет и использование архивных документов, а также материально-техническое и иное обеспечение в Управлении осуществляются в установленном порядке.
</p>
<p align="center">
	  <strong>V</strong><strong>. Финансовое обеспечение Управления</strong>
</p>
<p>
	  23. Финансирование Управления осуществляется ФМС России за счет средств, выделяемых из федерального бюджета.
</p>
<p>
	 24. Управление в установленном порядке ведет бюджетный учет, составляет статистическую и бюджетную отчетность, представляет ее в установленные сроки в ФМС России, налоговые органы, внебюджетные фонды и другие организации в соответствии с законодательством Российской Федерации.
</p>
