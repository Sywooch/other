</p>
<p align="center">
	<strong> I</strong><strong>. Общие положения</strong>
</p>
<p>
	<strong> </strong>1. Управление Федеральной миграционной службы по Ярославской области<a title="" href="http://ufmsyar.ru/ufms/korotko#_ftn1" style="color: #00749e;">[1]</a> является территориальным органом Федеральной миграционной службы в субъекте Российской Федерации, осуществляющим правоприменительные функции, функции по контролю, надзору и оказанию государственных услуг в сфере миграции на территории Ярославской области.
</p>
<p>
	 2. Настоящее положение определяет основные задачи, полномочия, права и организацию деятельности Управления.
</p>
<p>
	 3. Руководство Управлением и контроль его деятельности осуществляет Федеральная миграционная служба<a title="" href="http://ufmsyar.ru/ufms/korotko#_ftn2" style="color: #00749e;">[2]</a>.
</p>
<p>
	 4. Управление является юридическим лицом, имеет обособленное имущество, закрепленное за ним в установленном порядке, лицевые счета в территориальном органе Федерального казначейства, самостоятельный баланс, печать с изображением Государственного герба Российской Федерации и своим наименованием, а также соответствующие бланки, печати и штампы.
</p>
<p>
	 5. Полное наименование территориального органа:
</p>
<p>
	 Управление Федеральной миграционной службы по Ярославской области.
</p>
<p>
	 Краткое наименование территориального органа:
</p>
<p>
	 УФМС России по Ярославской области.
</p>
<p>
	 Юридический адрес территориального органа: 150000, г. Ярославль,   ул. Собинова, д. 48. Фактический адрес территориального органа: 150000, г. Ярославль,   ул. Собинова, д. 48.
</p>
<p>
	 6. Основными задачами Управления являются:
</p>
<p>
	 6.1. Производство по делам о гражданстве Российской Федерации, оформление и выдача документов, удостоверяющих личность гражданина Российской Федерации.
</p>
<p>
	 6.2. Осуществление регистрационного учета граждан Российской Федерации по месту пребывания и по месту жительства в пределах Российской Федерации.
</p>
<p>
	 6.3. Оформление и выдача иностранным гражданам и лицам без гражданства документов для въезда в Российскую Федерацию, проживания и временного пребывания в Российской Федерации, а также для осуществления трудовой деятельности на территории Российской Федерации.
</p>
<p>
	 6.4. Осуществление федерального государственного контроля (надзора) в сфере миграции.
</p>
<p>
	 6.5. Осуществление миграционного учета иностранных граждан и лиц без гражданства в Российской Федерации.
</p>
<p>
	 6.6. Реализация во взаимодействии с другими территориальными органами федеральных органов исполнительной власти мер по предупреждению и пресечению незаконной миграции.
</p>
<p>
	 6.7. Исполнение законодательства Российской Федерации по вопросам беженцев и вынужденных переселенцев, участие в установленном порядке в предоставлении политического убежища иностранным гражданам и лицам без гражданства.
</p>
<p>
	 7. Управление в своей деятельности руководствуется Конституцией Российской Федерации, федеральными конституционными законами, федеральными законами, актами Президента Российской Федерации и Правительства Российской Федерации, международными договорами Российской Федерации, Положением о Федеральной миграционной службе, утвержденным постановлением Правительства Российской Федерации от 13 июля 2012 г. № 711, приказами и распоряжениями ФМС России, а также настоящим Положением.
</p>
<p>
	 8. Деятельность Управления осуществляется в соответствии с принципами уважения прав и свобод человека и гражданина, законности и гуманизма, на основе планирования, сочетания единоначалия в решении вопросов служебной деятельности и коллегиальности при их обсуждении, персональной ответственности должностных лиц Управления за состоянием дел на порученном участке и выполнением Управлением отдельных поручений.
</p>
<p>
	 9. Управление создается, преобразуется и ликвидируется в порядке, установленном законодательством Российской Федерации.
</p>
<p>
	 10. Территория осуществления полномочий Управления и его компетенция устанавливается настоящим Положением.
</p>
<p>
	 11. Положение об Управлении, структура Управления, штатное расписание, предельная численность, фонд оплаты труда, а также смета доходов и расходов на его содержание утверждаются руководителем ФМС России.
</p>
<p>
	 12. Управление имеет структурные подразделения по направлениям деятельности, межрайонного, районного и городского уровня.
</p>
<p>
	 13. Должности в Управлении могут замещаться в установленном порядке сотрудниками органов внутренних дел Российской Федерации, прикомандированными к ФМС России<a title="" href="http://ufmsyar.ru/ufms/korotko#_ftn3" style="color: #00749e;">[3]</a>, федеральными государственными гражданскими служащими<a title="" href="http://ufmsyar.ru/ufms/korotko#_ftn4" style="color: #00749e;">[4]</a> и работниками.
</p>
<p>
	 14. Управление осуществляет свою деятельность во взаимодействии с территориальными органами других федеральных органов исполнительной власти. При решении совместных задач Управление обеспечивает необходимое взаимодействие с органами исполнительной власти субъектов Российской Федерации, органами местного самоуправления, общественными объединениями и иными организациями.
</p>
<p align="center">
	  <strong>II</strong><strong>. Полномочия Управления</strong>
</p>
<p>
	<strong> </strong>15. Управление осуществляет следующие полномочия:
</p>
<p>
	 15.1. Осуществляет федеральный государственный надзор (контроль):
</p>
<p>
	 1) за соблюдением гражданами Российской Федерации и должностными лицами правил регистрации и снятия граждан Российской Федерации с регистрационного учета по месту пребывания и по месту жительства в пределах Российской Федерации;
</p>
<p>
	 2) за соблюдением иностранными гражданами и лицами без гражданства, должностными лицами, юридическими лицами, иными органами и организациями правил миграционного учета;
</p>
<p>
	 3) за соблюдением иммиграционных правил иностранными гражданами и лицами без гражданства;
</p>
<p>
	 4) за соблюдением правил привлечения работодателями, заказчиками работ (услуг) иностранных работников в Российскую Федерацию и использования их труда;
</p>
<p>
	 5) за соблюдением лицензиатом лицензионных требований и условий, связанных с трудоустройством граждан Российской Федерации за пределами Российской Федерации;
</p>
<p>
	 6) за соблюдением правил трудовой деятельности иностранными работниками на территории Российской Федерации;
</p>
<p>
	 7) за соблюдением иностранными гражданами и лицами без гражданства   сроков пребывания (проживания), передвижения или порядка выбора места пребывания (жительства) на территории Российской Федерации;
</p>
<p>
	 8) за исполнением иностранным гражданином или лицом без гражданства назначенного ему административного наказания в виде административного выдворения за пределы Российской Федерации в форме контролируемого самостоятельного выезда из Российской Федерации.
</p>
<p>
	 15.2. Принимает в соответствии с законодательством Российской Федерации решения:
</p>
<p>
	 1) по заявлениям об изменении гражданства Российской Федерации;
</p>
<p>
	 2) о продлении либо сокращении срока временного пребывания иностранных граждан или лиц без гражданства в Российской Федерации;
</p>
<p>
	 3) о выдаче либо об отказе в выдаче иностранным гражданам или лицам без гражданства разрешения на временное проживание в Российской Федерации или вида на жительство, о продлении срока действия вида на жительство, а также об аннулировании разрешения на временное проживание или вида на жительство;
</p>
<p>
	 4) о выдаче, приостановлении срока действия или аннулировании разрешений на привлечение и использование иностранных работников, разрешений иностранным гражданам или лицам без гражданства на работу и патентов, лицензий на осуществление деятельности, связанной с трудоустройством граждан Российской Федерации за пределами Российской Федерации;
</p>
<p>
	 5) о признании иностранного гражданина или лица без гражданства беженцем, о продлении и лишении статуса беженца, о предоставлении и лишении временного убежища на территории Российской Федерации, о приеме к рассмотрению ходатайства о предоставлении Российской Федерацией политического убежища, а также о выдаче проездного документа, выдаваемого беженцу;
</p>
<p>
	 6) об установлении личности иностранного гражданина или лица без гражданства, незаконно находящегося на территории Российской Федерации и не имеющего действительного документа, удостоверяющего личность, а также в иных случаях, предусмотренных законодательством Российской Федерации или международным договором Российской Федерации.
</p>
<p>
	 15.3. Осуществляет в соответствии с законодательством Российской Федерации:
</p>
<p>
	 1) оформление и выдачу работодателям, заказчикам работ (услуг) разрешений на при влечение в Российскую Федерацию и использование иностранных работников, разрешений на работу иностранным гражданам и лицам без гражданства, а также патентов для осуществления иностранными гражданами и лицами без гражданства трудовой деятельности у физических лиц;
</p>
<p>
	          2) выдачу иностранным гражданам и лицам без гражданства виз на въезд в Российскую Федерацию, их продление, аннулирование и восстановление;
</p>
<p>
	          3) выдачу иностранным гражданам и лицам без гражданства разрешения на временное проживание в Российской Федерации;   
</p>
<p>
	          4) выдачу иностранным гражданам и лицам без гражданства вида на жительство в Российской Федерации;
</p>
<p>
	          5) определение и оформление наличия гражданства Российской Федерации у лиц, проживающих на территории Российской Федерации;
</p>
<p>
	          6) принятие от лиц, проживающих на территории Российской Федерации, заявлений по вопросам гражданства Российской Федерации;
</p>
<p>
	          7) составление заключений на заявления об изменении гражданства Российской Федерации в общем порядке и направление их в ФМС России;
</p>
<p>
	          8) исполнение решений по заявлениям об изменении гражданства Российской Федерации;
</p>
<p>
	          9) ведение учета лиц,в отношении которых территориальным органом приняты решения об изменении гражданства;
</p>
<p>
	          10) отмену принятых территориальным органом решений по вопросам гражданства Российской Федерации;
</p>
<p>
	          11) выдачу и замену гражданам Российской Федерации основных документов, удостоверяющих их личность на территории Российской Федерации;
</p>
<p>
	          12) регистрационный учет граждан Российской Федерации по месту пребывания и по месту жительства в пределах Российской Федерации;
</p>
<p>
	          13) миграционный учет иностранных граждан и лиц без гражданства;
</p>
<p>
	          14) оформление и выдачу паспортов гражданам Российской Федерации для выезда из Российской Федерации и въезда в Российскую Федерацию;
</p>
<p>
	 15) учет выданных, утраченных, похищенных и признанных недействительными паспортов гражданина Российской Федерации;
</p>
<p>
	 16) оформление и выдачу приглашений иностранным гражданам и лицам без гражданства на въезд в Российскую Федерацию, а также ведение учета оформляющих пpиглашения юридических лиц;    
</p>
<p>
	 17) реализацию мер по предупреждению и пресечению незаконной миграции, иммиграционный контроль в отношении иностранных граждан и лиц без гражданства, в том числе совместно с контрольно-надзорными и правоохранительными органами;
</p>
<p>
	 18) проставление отметки о запрещении въезда в Российскую Федерацию в документе, удостоверяющем личность иностранного гражданина или лица без гражданства;
</p>
<p>
	 19) проведение государственной дактилоскопической регистрации;
</p>
<p>
	  20) прием и рассмотрение ходатайств о предоставлении Российской Федерацией политического убежища, о признании беженцем и заявлений о предоставлении временного убежища на территории Российской Федерации;
</p>
<p>
	 21) идентификацию личности иностранных граждан и лиц без гражданства, ходатайствующих о признании беженцем на территории Российской Федерации;
</p>
<p>
	 22) выдачу и продление срока действия свидетельства о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу, удостоверения беженца, проездного документа, выдаваемого беженцу, и свидетельства о предоставлении. временного убежища на территории Российской Федерации;
</p>
<p>
	 23) выдачу справки о рассмотрении заявления о предоставлении временного убежища на территории Российской Федерации;
</p>
<p>
	 24) направление иностранных граждан и лиц без гражданства, получивших свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу, признанных беженцами и получивших временное убежище на территории Российской Федерации в центры временного размещения;
</p>
<p>
	 25) содействие в обеспечении проезда и провоза багажа к месту пребывания иностранным гражданам или лицам без гражданства, получившим свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу и признанным беженцами, а также членам их семей;
</p>
<p>
	 26) сопровождение иностранных граждан или лиц без гражданства, получивших свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу, и членов их семей в центр временного размещения;
</p>
<p>
	 27) обеспечение предоставления услуг переводчика лицам, получившим свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу, и лицам, признанным беженцами или получившим временное убежище на территории Российской Федерации;
</p>
<p>
	 28) выдачу направления иностранным гражданам или лицам без гражданства, получившим свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу, на получение единовременного пособия;
</p>
<p>
	 29) направление иностранных граждан и лиц без гражданства, получивших свидетельство о рассмотрении ходатайства о признании беженцем на территории Российской Федерации по существу на обязательное медицинское освидетельствование;
</p>
<p>
	 30) ведение учета иностранных граждан и лиц без гражданства, ходатайствующих о признании беженцами, признанных беженцами, и членов их семей, а также получивших временное убежище, с последующим внесением информации в центральный банк данных по учету иностранных граждан и лиц без гражданства в Российской Федерации;
</p>
<p>
	 31) постановку на учет и снятие с учета иностранных граждан и лиц без гражданства, признанных беженцами, и членов их семей при перемене ими места пребывания;
</p>
<p>
	 32) прием иностранных граждан и лиц без гражданства, ходатайствующих о признании беженцем или предоставлении временного убежища, в случае их экстренного массового прибытия на территории Российской Федерации;
</p>
<p>
	 33) учет, регистрацию, прием и временное размещение лиц, ходатайствующих о признании их вынужденными переселенцами, получивших статус вынужденного переселенца, а также оказание содействия вынужденным переселенцам в обустройстве на новом месте жительства на территории осуществления полномочий;
</p>
<p>
	 34) учет и фотографирование иностранных граждан и лиц без гражданства, подлежащих передаче в соответствии с международными договорами Российской Федерации о реадмиссии либо принимаемых в Соответствии с международными договорами Российской Федерации о реадмиссии;
</p>
<p>
	 35) исполнение решений о реадмиссии иностранных граждан и лиц без гражданства и временное размещение иностранных граждан и лиц без гражданства, подлежащих передаче в соответствии с международными договорами Российской Федерации о реадмиссии, либо иностранных граждан и лиц без гражданства, принимаемых в соответствии с международными договорами Российской Федерации о реадмиссии, но не имеющих законных оснований для пребывания (проживания) в Российской Федерации;
</p>
<p>
	 36) оформление, выдачу и замену свидетельства участника Государственной программы по оказанию содействия добровольному переселению в Российскую Федерацию соотечественников, проживающих за рубежом;
</p>
<p>
	 37) учет участников Государственной программы по оказанию содействия добровольному переселению в Российскую Федерацию соотечественников, проживающих за рубежом;
</p>
<p>
	 38) производство по делам об административных правонарушениях в пределах установленной компетенции;
</p>
<p>
	 39) содержание в специальных учреждениях иностранных граждан и лиц без гражданства, подлежащих административному выдворению за пределы Российской Федерации, депортации или реадмиссии<a title="" href="http://ufmsyar.ru/ufms/korotko#_ftn5" style="color: #00749e;">[5]</a>;
</p>
<p>
	 40) представление в установленном порядке в судах интересов Президента Российской Федерации, Правительства Российской Федерации и ФМС России по вопросам своей компетенции, а также защиту в судах интересов территориального органа;
</p>
<p>
	 41) подготовку и предоставление в установленном порядке и в установленные сроки ведомственной и государственной статистической отчетности.
</p>
<p>
	 15.4. Подготавливает, утверждает и исполняет в соответствии, с законодательством Российской Федерации решения о депортации иностранных граждан и лиц без гражданства, а также об установлении запрета на въезд в Российскую Федерацию иностранных граждан и лиц без гражданства.
</p>
<p>
	 15.5. Осуществляет в установленном порядке мероприятия по административному выдворению за пределы Российской Федерации иностранных граждан и лиц без гражданства.
</p>
<p>
