<h3 align="left" style="color: #9e131b;">Предоставление адресно-справочной информации осуществляется <span style="text-decoration: underline;">бесплатно.</span></h3>
<h4 align="center">Результатом предоставления государственной услуги является:</h4>
<p>
	 ·         предоставление адресно-справочной информации в случаях, предусмотренных законодательством Российской Федерации;
</p>
<p>
	 ·         отказ в предоставлении адресно-справочной информации в случаях, предусмотренных законодательством Российской Федерации;
</p>
<p>
	 ·         внесение и корректировка учетных сведений в адресно-справочных учетах.
</p>
<h4 align="center">Общие сроки предоставления государственной услуги:</h4>
<p>
	 Срок исполнения обращения, направленного в письменной (электронной) форме по вопросам, носящим консультационный характер, не должен превышать 15 рабочих дней.
</p>
<p>
	 Срок исполнения письменного запроса (заявления) в случае, когда согласие субъекта адресно-справочной информации на предоставление информации обязательно, не может превышать трех рабочих дней с момента получения такого согласия, выраженного в письменной форме.
</p>
<p>
	 При этом общий срок исполнения подразделением АСР письменных (электронных) обращений о предоставлении адресно-справочной информации в случае, когда получение согласия субъекта адресно-справочной информации обязательно, не должен превышать 30 дней.
</p>
<p>
	 Общий срок исполнения подразделениями АСР письменных (электронных) обращений о предоставлении адресно-справочной информации в случае, когда получение согласия субъекта адресно-справочной информации не обязательно, не должен превышать 15 рабочих дней. 
</p>
