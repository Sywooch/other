<h3 align="center" style="color: #9e131b;">Порядок предоставления государственной услуги </h3>
<h4>Обращение (заявление) физического лица подлежит исполнению подразделением АСР при выполнении одного из следующих условий:</h4>
<p>
	 ·         гражданин направил заявление о предоставлении адресно-справочной информации в целях ознакомления с учетными сведениями в отношении себя;
</p>
<p>
	 ·         гражданин направил заявление в целях исправления ошибок в учетных сведениях в отношении себя, а также в связи с их изменениями;
</p>
<p>
	 ·         гражданин направил заявление о предоставлении адресно-справочной информации в целях открытия (закрытия) общего доступа ко всему или отдельному перечню учетных сведений в отношении себя;
</p>
<p>
	 ·         получено согласие субъекта адресно-справочной информации на предоставление запрашиваемой информации, в случае если такое согласие обязательно.
</p>
<h4>Письменное (электронное) обращение (заявление) физического лица о предоставлении адресно-справочной информации должно содержать следующие обязательные реквизиты заявителя:</h4>
<p>
	 ·         фамилию, имя, отчество, дату рождения, дату обращения, личную подпись и почтовый адрес заявителя;
</p>
<p>
	 ·         полные данные на запрашиваемое лицо (фамилия, имя, отчество в именительном падеже, дата и место рождения);
</p>
<p>
	 ·         согласие заявителя на предоставление его персональных данных запрашиваемому им лицу.
</p>
<p>
	 Обращение (запрос) органа государственной власти, органа местного самоуправления, юридического лица, общественного объединения подлежит исполнению подразделением АСР при выполнении следующих условий:
</p>
<p>
	 ·         письменное (электронное) обращение (запрос) должно содержать обязательные реквизиты инициатора обращения (наименование органа (юридического лица), юридический, почтовый адрес, фамилию, инициалы, должность лица, подписавшего запрос, фамилию, инициалы, контактный телефон непосредственного исполнителя запроса, основание и цель обращения, ссылку на норму нормативного правового акта Российской Федерации, в соответствии с которой у инициатора запроса возникает право запрашивать персональные данные у оператора персональных данных, полные (фамилия, имя, отчество в именительном падеже, дата и место рождения) данные на запрашиваемое лицо;
</p>
<p>
	 ·         получено согласие субъекта адресно-справочной информации на предоставление запрашиваемых сведений, в случае если такое согласие обязательно. 
</p>
