	  <span style="text-decoration: underline; color: #993300; font-weight: bold;">Адресно-справочная работа</span>
</p>
<p>
	 Отдел адресно-справочной работы УФМС России по Ярославской области оказывает государственную услугу по предоставлению физическим и юридическим лицам персональных данных граждан Российской Федерации, включающих в себя паспортные данные и сведения о регистрации и снятии граждан Российской Федерации с регистрационного учета по месту пребывания или месту жительства в пределах Российской Федерации.
</p>
<p>
	 Отдел адресно-справочной работы УФМС России по Ярославской области находится по адресу: 150000, г. Ярославль, ул. Республиканская, дом 23, подъезд 1, телефон 8-(4852)-30-75-14, факс 8-(4852)-79-55-78, эл.почта: <a href="mailto:oab@heap.yar.mvd.ru" style="color: #00749e;">oab@heap.yar.mvd.ru</a>
</p>
<p align="center">
	<strong>Получателями государственной услуги являются:</strong>
</p>
<ul>
	<li>физические лица (граждане Российской Федерации, иностранные граждане и лица без гражданства);</li>
	<li>юридические лица и общественные объединения;</li>
	<li>органы государственной власти и местного самоуправления.</li>
</ul>
<p>
	<strong>Предметом адресно-справочной работы (АСР) является</strong> информация о физических лицах (гражданах Российской Федерации, иностранных гражданах и лицах без гражданства), включающая в себя:
</p>
<ul>
	<li>фамилия, имя, отчество, пол, дата и место рождения, гражданство;</li>
	<li>дата и адрес регистрации по месту жительства, снятия с регистрационного учета по месту жительства;</li>
	<li>дата, адрес и заявленный срок регистрации по месту пребывания;</li>
	<li>реквизиты документа, удостоверяющего личность (серия, номер, дата выдачи, орган, его выдавший);</li>
	<li>наименование населенного пункта субъекта Российской Федерации, страны, куда убыл или откуда прибыл гражданин;</li>
	<li>данные о фамилии, имени, отчестве и поле, с которых (на которые) были изменены указанные сведения;</li>
	<li>иная информация в соответствии с законодательством Российской Федерации.</li>
</ul>
