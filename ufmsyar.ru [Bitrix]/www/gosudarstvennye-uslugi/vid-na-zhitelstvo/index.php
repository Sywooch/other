?><h1 style="color: #464646; background-color: #ffffff;">Вид на жительство</h1>
<p align="justify">
	 С заявлением о получении вида на жительство иностранный гражданин, получивший временное проживание в Российской Федерации, может обратиться прожив на территории России&nbsp;<strong>не менее одного года</strong>&nbsp;с момента получения разрешения на временное проживание (РВП).
</p>
<p align="justify">
	 Вид на жительство выдается сроком на 5 лет, но не более чем на срок действия документа, удостоверяющего личность. Вид на жительство может быть продлен по заявлению иностранного гражданина, заявление подается&nbsp;<strong>не позднее чем за 6 месяцев</strong>&nbsp;до истечения срока его действия.
</p>
<p align="justify">
	 Срок рассмотрения заявления не более&nbsp;6 месяцев, госпошлина за выдачу вида на жительство в размере&nbsp;<strong>2000 рублей.</strong>
</p>
<p align="justify">
	<span style="text-decoration: underline;"><strong>Перечень документов, необходимый для получения вида на жительство:</strong></span>
</p>
<p align="justify">
	 - Заявление в 2-х экземплярах (установленной формы). В случае если заявитель намеревается получить разрешение для себя и совместно проживающих с ним членов семьи, то об этом указывается в заявлении. Заявление подается не позднее чем за шесть месяцев до истечения срока действия разрешения на временное проживание.
</p>
<p align="justify">
	 &nbsp;- 4 фотографии, размером (35 x 45 мм в черно-белом или цветном исполнении) в том числе 2 фотографии на несовершеннолетних детей, указанных в заявлении.
</p>
<p align="justify">
	 - Документы, удостоверяющие личность и гражданство.
</p>
<p align="justify">
	 - Разрешение на временное проживание, оформленное в установленном порядке.
</p>
<p align="justify">
	 - Документ, подтверждающий наличие у заявителя средств,&nbsp;обеспечивающих ему и членам его семьи при проживании в Российской Федерации прожиточный минимум, или документ, подтверждающий его нетрудоспособность (справка о доходах физического лица, декларация о доходах с отметкой налогового органа, справка с места работы, трудовая книжка, пенсионное удостоверение, справка органа социальной защиты о получении пособий, подтверждение о получении алиментов, справка о наличии вклада в кредитном учреждении с указанием номера счета, свидетельство о праве на наследство, справка о доходах лица, на иждивении которого находится заявитель, иной документ, подтверждающий получение доходов от не запрещенной законом деятельности или нетрудоспособность).<br>
</p>
<p align="justify">
	 - Свидетельство о рождении ребенка и документ, удостоверяющий личность ребенка, не достигшего 18-летнего возраста (паспорт - при его наличии).
</p>
<p align="justify">
	 - Согласие ребенка в возрасте от 14 до 18 лет на проживание в Российской Федерации. Подпись ребенка на документе должна быть нотариально удостоверена.
</p>
<p align="justify">
	 - Документ, подтверждающий наличие жилого помещения.
</p>
<p align="justify">
	 - Сертификат об отсутствии у заявителя (членов его семьи) ВИЧ-инфекции.
</p>
<p align="justify">
	 - Медицинское заключение, подтверждающие отсутствие заболевания наркоманией и инфекционных заболеваний у заявителя (членов его семьи).
</p>
<p>
	 В связи с изменениями, внесенными 30.12.2012 в п. 3 ст. 8 Федерального закона от 25 июля 2002г. № 115-ФЗ «О правовом положении иностранных граждан в Российской Федерации» вид на жительство выдается иностранному гражданину на пять лет. По окончании срока действия вида на жительство данный срок по заявлению иностранного гражданина, поданному в территориальный орган федерального органа исполнительной власти в сфере миграции не позднее, чем за два месяца до истечения срока действия имеющегося у него вида на жительство, может быть продлен на пять лет. Количество продлений срока действия вида на жительство не ограничено.
</p>
<p>
	 После окончания срока действия вида на жительство необходим выехать с территории Российской Федерации
</p>
