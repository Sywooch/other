<p>
	 Неисполнение беженцем или вынужденным переселенцем при перемене места пребывания или места жительства обязанности сняться с учета в территориальном органе федерального органа исполнительной власти, уполномоченного на осуществление функций по контролю и надзору в сфере миграции, к которому он прикреплен, а равно неисполнение обязанности своевременно встать на учет в территориальном органе федерального органа исполнительной власти, уполномоченного на осуществление функций по контролю и надзору в сфере миграции, по новому месту пребывания или месту жительства либо несообщение беженцем в соответствующий территориальный орган федерального органа исполнительной власти, уполномоченного на осуществление функций по контролю и надзору в сфере миграции, об изменении фамилии, имени, семейного положения, состава семьи, а также о приобретении гражданства Российской Федерации или гражданства (подданства) иного государства -<br>
