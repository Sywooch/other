?><h1 style="color: #464646; background-color: #ffffff;">Паспорт гражданина Российской Федерации</h1>
 Паспорт гражданина Российской Федерации является основным документом, удостоверяющим личность гражданина Российской Федерации на территории Российской Федерации.<br>
 Паспорт обязаны иметь все граждане Российской Федерации, достигшие 14-летнего возраста и проживающие на территории Российской Федерации.<br>
 Срок действия паспорта гражданина:<br>
 От 14 лет - до достижения 20-летнего возраста;<br>
 От 20 лет - до достижения 45-летнего возраста;<br>
 От 45 лет - бессрочно.<br>
