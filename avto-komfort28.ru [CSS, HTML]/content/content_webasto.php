запрещается стоянка с работающим двигателем в жилой 
зоне, в том числе на дворовых территориях. Согласно <a href="http://www.consultant.ru/popular/koap/13_13.html#p3933" style="text-decoration:underline; color:#666666">ст. 12.28 Кодекса об административных 
