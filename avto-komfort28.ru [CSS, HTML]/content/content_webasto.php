<h1 class="h1_style">Принцип действия и установка в автомобиль</h1>
<img src="images/webasto1.jpg" alt="" style="padding-bottom:20px; padding-right:20px; float:left"/>
<p class="p_style">
Предпусковой подогреватель устанавливается в моторном отсеке, подключается к системе охлаждения, топливной 
системе и бортовой электросети автомобиля. За счет сжигания топлива, подаваемого из бака специальным насосом, 
подогреватель нагревает жидкость в системе охлаждения автомобиля. Еще одним, жидкостным, собственным насосом 
он прокачивает ее по контуру, прогревая двигатель и, если это предусмотрено при установке, радиатор штатного 
отопителя. Когда контур достаточно разогревается, блок управления подогревателя автоматически включает 
вентилятор штатного отопителя, и тёплый воздух подаётся в салон. 
Схему подогревателя (на примере модели Thermo Top Evo) в разрезе можно посмотреть 
<a href="http://www.webasto.ru/products/lot01/razrez.pdf" style="text-decoration:underline; color:#666666">здесь</a>. 
</p>
<p class="p_style">
Работа подогревателя регулируется автоматически для оптимизации потребления топлива и контролируется многоступенчатой системой защиты. 
Момент запуска, режим и продолжительность работы устанавливаются пользователем с помощью таймера и/или системы управления по телефону 
(см. ниже). Такая конструкция выгодно отличает предпусковой подогреватель от других систем автомобильного отопления. Он гарантированно 
обеспечивает запуск двигателя при температурах до -40°С и ниже. При этом подогрев происходит непосредственно перед началом движения 
(в течение нескольких десятков минут), а машина совершенно не привязана к месту (в отличие от различных электрических систем с внешним 
источником энергии). Температура в салоне существенно повышается (при умеренном холоде снаружи – до комнатной); если же этого 
недостаточно, можно быстро прогреть салон разогретым двигателем, либо использовать конфигурацию с преимущественным прогревом 
салона, более <a href="http://www.webasto.ru/products/lot03/index.php" style="text-decoration:underline; color:#666666">
мощную модель</a> предпускового подогревателя или 
<a href="http://www.webasto.ru/products/lot04/index.php" style="text-decoration:underline; color:#666666">автономный воздушный отопитель</a>. 
Советуем обратить внимание на то, что:</p>
