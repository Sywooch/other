а в Москве и Санкт-Петербурге - 3000 руб.</p>
<p class="p_style">&bull;&nbsp;Согласно 
<a href="http://protect.gost.ru/v.aspx?control=8&baseC=-1&page=0&month=-1&year=-1&search=&RegNum=1&DocOnPageCount=15&id=169171&pageK=8C0701D0-8BA6-4463-9EFE-C2D5B519581E" style="text-decoration:underline; color:#666666">
