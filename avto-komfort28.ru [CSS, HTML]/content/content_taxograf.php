<h1 class="h1_style">Международные перевозки</h1>

<p class="p_style">С 1992 ГОДА</p>
<p class="p_style" style="text-decoration:underline">Нормативная основа</p>
<p class="p_style">Европейское соглашение, касающееся работы экипажей транспортных средств, производящих 
международные автомобильные перевозки (ЕСТР), подписано в г. Женеве 1 июля 1970 г.</p>
</br>

<p class="p_style" style="text-decoration:underline">
Транспортные средства, подлежащие оснащению:</p>



<p class="p_style">&bull;&nbsp;Автотранспортные средства, используемые для перевозки 
грузов, разрешенный максимальный вес которых, включая вес прицепов или полуприцепов составляет более 3,5 тонн;</p>
<p class="p_style">&bull;&nbsp;Автотранспортные средства, используемые для перевозки пассажиров, которые в силу 
своей конструкции и оборудования могут перевозить более 9 человек;</p>
</br>
<p class="p_style" style="text-decoration:underline">Тип контрольного устройства:</p> 
<p class="p_style"><strong>До 16 июня 2010 года:</strong> аналоговые контрольные устройства</p>
<p class="p_style"><strong>С 16 июня 2010 года:</strong> цифровые контрольные устройства, отвечающие 
требованиям Приложения Добавления 1В к ЕСТР</p>
<p class="p_style"><strong>
С 1 октября 2012 года:</strong> цифровые контрольные устройства, отвечающие требованиям Приложения Добавления 1В к 
ЕСТР с учетом требований Постановления комиссии (ЕС) № 1266/2009 от 26 декабря 2009 г. о десятом согласовании с 
техническим прогрессом Постановления Совета (ЕЕС) 
№ 3821/85 о регистрирующем оборудовании на автомобильном транспорте, вступившим в действие с 1 октября 2012 года</p>
<p class="p_style">Цифровые устройства контроля за режимами труда и отдыха водителей, прошедшие процедуры 
официального утверждения типа в соответствии с требованиями ЕСТР</p>
 
