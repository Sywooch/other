<h1 class="h1_style">«Прамотроник 4Д-12», «Прамотроник 4Д-24»</h1> 
</br>
<div style="height:220px">
<img  src="images/pramotronic1.jpg" alt="pramotronic" style="float:left; padding-right:20px; padding-bottom:20px"/>

<p class="p_style">Воздушные отопители «Прамотроник» 12В и 24В предназначены для эффективного и быстрого 
разогрева воздуха в кабине (салоне) автотранспортных средств (грузовых автомобилей, микроавтобусов, 
вахтовок, кабин крановых, экскаваторов, КУНГов и многих других) и избавления от обледенения окон.</p>
<p class="p_style">Отопитель позволяет поддерживать зданную температуру в холодное время года при неработающем двигателе.</p>
</div>
<p class="p_style"><strong>
Преимущества данной модели над аналогами российского и зарубежного производства:</strong></p>
<p class="p_style">&bull;&nbsp;Современный дизайн и минимальные габаритные размеры;</p>
<p class="p_style">&bull;&nbsp;Компактная конструкция даёт возможность устанавливать 
отопитель в различных местах кабины или салона автомобиля;</p>
<p class="p_style">&bull;&nbsp;Отопитель «ПРАМОТРОНИК» является самым удобным из всех 
выпускаемых отопителей для сервисного и технического обслуживания;</p>
<p class="p_style">&bull;&nbsp;Программа блока управления отопителем производит учёт 
времени работы отопителя, количество запусков и возникавшие неисправности. Это 
позволяет при считывании информации, в сервисном центре, быстро устранить 
возникшую неисправность. Кроме этого, здесь осуществляется цифровая связь 
между отопителем и пультом управления, что позволяет интегрировать управление 
отопителя в бортовой компьютер автотранспортного средства;</p>
<p class="p_style">&bull;&nbsp;В случае возникновения какой-либо неисправности, на пульт 
управления отопителем выводится соответствующий световой и звуковой сигнал;</p>
<p class="p_style">&bull;&nbsp;Отопитель «Прамотроник» работает в автоматическом режиме, 
поддерживая заранее заданную температуру водителем на пульте управления, а выносной датчик 
температуры позволяет поддерживать заданную температуру в определённом месте салона и кабины</p>
<img src="images/pramotronic2.jpg" alt="pramotronic жидкостные подогреватели" style="float:right; padding-left:20px; padding-bottom:5px"/>
<p class="p_style">&bull;&nbsp;С пульта управления отопителем можно устанавливать комфортную вентиляцию
(без нагрева) в летний период года;</p>
<p class="p_style">&bull;&nbsp;В отопителе «ПРАМОТРОНИК» камера сгорания и теплообменник по своей 
конструкции не имеют аналогов среди отопителей мировых производителей и защищены патентами. 
Результат этих изобретений - минимальное содержание вредных веществ и выхлопных газов 
отопителя и простота конструкции теплообменника при повышении его тепловой эффективности;</p>
<p class="p_style">&bull;&nbsp;
Отопитель «ПРАМОТРОНИК» по заказу потребителей может комплектоваться «искрогасителем», что 
позволяет устанавливать его на автотранспорт, перевозящий легковоспламеняющиеся и 
взрывоопасные вещества. Искрогаситель также является глушителем шума от выхлопных газов;</p>
<p class="p_style">&bull;&nbsp;Низкий уровень шума является немаловажным фактором при 
эксплуатации отопителя «ПРАМОТРОНИК» в ночное время;</p>
<p class="p_style">&bull;&nbsp;Низкое потребление тока позволяет на стоянке автомобиля, не 
запускать двигатель 2-3 суток для подзарядки аккумуляторной батареи;</p>
<p class="p_style">&bull;&nbsp;Полный монтажный комплект включает пластиковый топливный бак (объём 12 л.).</p>
