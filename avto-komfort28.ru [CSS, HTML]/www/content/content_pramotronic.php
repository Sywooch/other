<h1 class="h1_style">«ПРАМОГРОНИК 16ЖД24»</h1>
<img src="images/pramotronic7.jpg" alt="pramotronic благовещенск" style="float:left; padding-right:20px; padding-bottom:20px; padding-top:10px"/>
<p class="p_style">Предпусковой жидкостный подогреватель «ПРАМОТРОНИК 16ЖД24» предназначен для разогрева и
поддержания теплового баланса дизельных двигателей на грузовых автомобилях, автобусах малого и среднего класса, 
спецтехники и коммерческого транспорта. Подогреватель разработан с учётом последних достижений российских и 
зарубежных производителей данной техники.</p>
<p class="p_style">Эта модель подогревателя отличается от своих аналогов, в которых используется камера 
сгорания испарительного типа, малыми габаритами и высокой тепловой производительностью. Преимущества 
достигнуты за счет оригинальной конструкции теплообменника и оптимально рассчитанной камеры сгорания. 
Для изготовления подогревателя, используются детали и комплектующие зарубежного производства, установлена 
бескорпусная, керамическая свеча, с большим ресурсом по числу включений.</p>
<p class="p_style">Компоновка блока управления на подогревателе позволила сократить до минимума 
длину и количество соединительных проводов. Герметичные разъёмы, расположенные на корпусе блока 
управления, позволили избавиться от жгутов, выходящих из нагревателя, что повысило 
влагозащищенность подогревателя и блока управления.</p>
<p class="p_style">Новая электронная схема блока управления и электронные датчики температуры 
позволили создать программу управления подогревателем, которая осуществляет диагностику 
исправности всех узлов подогревателя перед его запуском и в процессе работы подогревателя, 
а в случае возникновения неисправности автоматическое выключение подогревателя с выводом 
информации на индикатор пульта управления.</p>
<p class="p_style">В подогревателе применена система управления, обеспечивающая плавный 
запуск электродвигателя нагнетателя воздуха, нагрев свечи накаливания и плавное 
управление работой топливного насоса. Всё это позволяет осуществлять гарантированный 
запуск подогревателя, плавный переход с одного режима работы на другой, организовать 
оптимальное горение топлива в камере сгорания, т.е. минимизировать выбросы в атмосферу 
вредных примесей содержащихся в выхлопных газах.</p>
<p class="p_style">Включение и выключение подогревателя производится вручную с пульта управления.</p>
<p class="p_style">Подогреватель имеет две штатные программы работы. Первая программа 
предназначена для быстрого разогрева двигателя автомобиля, вторая программа для 
поддержания теплового баланса работающего двигателя и автоматического поддержания 
заданной комфортной температуры в кабине автомобиля. Регулировка комфортной 
температуры в кабине автомобиля осуществляется поворотом ручки на пульте управления.</p>
