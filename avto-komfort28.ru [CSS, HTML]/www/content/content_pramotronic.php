<p class="p_style">
Управление отопителем осуществляется с пульта управления. Пульт управления со звуковой и световой 
индикацией неисправностей отопителя. Звуковой сигнал разбудит спящего водителя для принятия мер. 
На панели пульта управле¬ния расположена ручка регулировки температуры в кабине (салоне) 
автотранспортного средства в диапазоне от 15 С до 32 С. Автоматическая система управления 
отопителя постоянно поддерживает заданную водителем температуру. Контроль температуры в кабине 
(салоне) автотранспортного средства контролирует встроенный или выносной датчик температуры.</p>
<p class="p_style">Связь между отопителем и пультом управления осуществляется по цифровому каналу, 
что позволяет снизить количество проводов и упростить монтаж.</p>
<p class="p_style">
Отопитель «ПРАМОТРОНИК 4Д-24» поставляется с монтажным комплектом, с помощью которого 
сервисный центр в течение нескольких часов произведет монтаж и запуск изделия.</p>
<p class="p_style">
Установку отопителя можно производить как в кабине (салоне) автотранспортного средства так и вне 
кабины, при условии обеспечения защиты от воздействия природных осадков. Забор воздуха для обогрева 
может производиться как из кабины (рециркуляция воздуха повышает эффективность отопителя), так и с 
наружи по гибким гофро трубам. Забор воздуха для горения и выход выхлопных газов должен осуществляться 
только снаружи кабины. Конструкция отопителя и квалифицированный монтаж исключают попадание выхлопных 
газов в кабину автотранспортного средства.</p>


