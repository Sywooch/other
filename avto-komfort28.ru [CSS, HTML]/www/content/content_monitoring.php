<p class="p_style">
Пакет услуг «Golden Eye Stealth» включает в себя установку на транспортное средство специализированного варианта
Системы. В пакет гарантировано входит базовая система поиска транспортного средства, которая активируется только
по звонку Абонента с предоставлением фактических данных об угоне автомобиля, либо в ходе периодической
проверки. Ни в каком другом случае доступ к Системе не производится (пассивный режим).</p>
</br>
<p class="p_style"><strong>1. Угон/попытка угона</strong></p>
<p class="p_style">По предоставлении Абонентом или его доверенными лицами фактических данных о том, что был совершен угон,
Оперативный Центр:</p>
<p class="p_style">&#10004;&nbsp;Пытается связаться с транспортным средством</p>
<p class="p_style">&#10004;&nbsp;Передает все необходимые данные (местонахождение транспортного средства, состояние транспортного средства
и т.д.) в силовые структуры для немедленного оперативного вмешательства</p>
<p class="p_style">&#10004;&nbsp;По возможности будет осуществлять связь с транспортным средством, продолжая отслеживать изменения
местоположения и изменения в состоянии транспортного средства, немедленно передавая эти данные в
задействованные силовые структуры</p>
<p class="p_style">&#10004;&nbsp;При необходимости и наличии возможности координирует возврат транспортного средства и его защиту, по
возможности согласовывая действия с Абонентом</p>
<p class="p_style">&#10004;&nbsp;Информирует Абонента или доверенных лиц Абонента о результатах оперативного вмешательства</p>
</br>
<p class="p_style"><strong>2. Периодическая проверка</strong></p>
<p class="p_style">Периодическая проверка производится путем пересылки проверочных сигналов с информацией о состоянии системы,
обычно от Системы в Оперативный Центр, с возможностью соответствующему Оперативному Центру (в дальнейшем
ОЦ) осуществлять обратный вызов Системы если должный сигнал не был получен.</p>
<p class="p_style">При реагировании на пропущенные проверки и неудачную попытку обратного вызова к Системе, Оперативный Центр:</p>
<p class="p_style">&#10004;&nbsp;Пытается связаться с Системой</p>
<p class="p_style">&#10004;&nbsp;В случае, если подключиться удалось, проверит состояние Системы</p>
<p class="p_style">&#10004;&nbsp;В случае, если связаться с Системой не удалось, ОЦ свяжется с Абонентом любыми доступными способами, для
для выяснения ситуации и проведения проверки Системы.</p>
<p class="p_style">Проверка может быть проведена только в случае, если имеется возможность связи с Системой.</p>
</br>
<p class="p_style"><strong>3. Услуга по запросу</strong></p>
<p class="p_style">Услуга по запросу может быть оказана только в случае, если на транспортное средство установлена
работающая Система, конфигурация Системы позволяет производить подключение к ней и только по письменной
заявке Абонента, переданной в Оперативный Центр.</p>
<p class="p_style">Услуга по запросу будет оказана по стоимости, указанной в действующем прайс-листе, и должна быть
оплачена отдельно в соответствии с Договором о предоставлении Услуг, подписанным Абонентом.</p>
</br>
<p class="p_style"><strong>4. Изменения конфигурации</strong></p>
