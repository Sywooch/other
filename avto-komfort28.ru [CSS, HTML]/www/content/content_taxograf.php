<p class="p_style">
<strong>ВОЗМОЖНОСТИ "Штрих-ТахоRUS": </strong></p>

<p class="p_style">&bull;&nbsp;Контроль за режимами труда и отдыха водителей; </p> 
<p class="p_style">&bull;&nbsp;Накопление и хранение данных за 366 дней; </p>
<p class="p_style">&bull;&nbsp;Индикация и сохранение в памяти устройства скорости, пробега ТС, режимов работы водителей; </p>
<p class="p_style">&bull;&nbsp;Пределы регистрации скорости 0-250 км/ч; </p>
<p class="p_style">&bull;&nbsp;Часы реального времени с погрешностью не более ±0,05 сек/ сутки (в исполнении без приёмника ГЛOНАСС/GPS - не более ±2 сек/сутки); </p>
<p class="p_style">&bull;&nbsp;Встроенная самодиагностика установленных компонентов; </p>
<p class="p_style">&bull;&nbsp;Получение информации о скорости со следующих устройств: 
<p class="p_style" style="margin-left:20px">&bull;&nbsp;с датчика скорости, имеющего криптографическую защиту информации; 
<p class="p_style" style="margin-left:20px">&bull;&nbsp;со штатного датчика скорости транспортного средства; 
<p class="p_style" style="margin-left:20px">&bull;&nbsp;с приёмника ГЛОНАСС/ GPS; 
<p class="p_style" style="margin-left:20px">&bull;&nbsp;с датчика системы ABS автомобиля; 
<p class="p_style" style="margin-left:20px">&bull;&nbsp;с акселерометра. </p>
<p class="p_style">&bull;&nbsp;Распечатка всех отчетов (либо вывод на дисплей) по требованию согласно ЕСТР., </p>
<p class="p_style">&bull;&nbsp;Возможность удаленного мониторинга работы автомобиля и КУ, </p>
<p class="p_style">&bull;&nbsp;Контроль и регистрация за отключением КУ от бортовой сети автомобиля, </p>
<p class="p_style">&bull;&nbsp;Возможность получения информации по CAN интерфейсу, </p>
<p class="p_style">&bull;&nbsp;Возможность мониторинга местоположения транспортного средства, </p>
<p class="p_style">&bull;&nbsp;Передача информации о местоположении транспортного средства в режиме реального времени через GPRS / GSM - модем, </p>
<p class="p_style">&bull;&nbsp;Передача данных с контрольного устройства по радиоканалу ближнего радиуса действия. </p>
</br>
<p class="p_style">
КУ регистрирует и хранит различные данные: </p>
<p class="p_style">&bull;&nbsp;данные карты водителя, за исключением данных водительского удостоверения. </p>
<p class="p_style">&bull;&nbsp;предупреждения и неисправности, связанные с КУ, водителем, компанией и мастерской. </p>
<p class="p_style">&bull;&nbsp;сведения об автомобиле, данные одометра и подробные данные о скорости. </p>
<p class="p_style">&bull;&nbsp;вмешательство в контрольное устройство (случаи доступа к данным регистрации, перерывов в 
электропитании длительностью более 100 миллисекунд, перерывов в подаче импульсов от датчика движения). </p>
</br>
<p class="p_style">Применение "Штрих-ТахоRUS" позволяет решить следующие задачи:</p>
<p class="p_style">&bull;&nbsp;повысить безопасность движения за счет записи и непрерывного контроля параметров скорости 
движения транспортного средства и времени нахождения водителей за рулем; </p>
<p class="p_style">&bull;&nbsp;исключить несанкционированные перевозки и приписки невыполненных работ, так как фактические 
значения пробега и рабочего времени постоянно записываются на внутреннюю память КУ и карту водителя, которая является 
персональной для каждого водителя и подлежит учету; </p>
<p class="p_style">&bull;&nbsp;увеличить срок эксплуатации двигателя, шин, тормозных систем и автомобиля в целом, а также снизить потребление 
топлива и расходы на ТО и, следовательно, расходы на эксплуатацию в целом, в результате соблюдения водителем предписанных режимов движения; </p>
<p class="p_style">&bull;&nbsp;обеспечить правовую защиту водителей в случаях, связанных с уголовной или страховой ответственностью, 
возникших в результате совершения дорожно-транспортного происшествия; </p>
<p class="p_style">&bull;&nbsp;позволить осуществлять объективные расчеты с заказчиками по фактическим объемам выполненных работ 
по перевозке грузов и за простои по различным причинам; </p>
<p class="p_style">&bull;&nbsp;оптимизировать маршруты перевозок и графиков движения по результатам сравнительного анализа записей в памяти КУ; </p>
<p class="p_style">&bull;&nbsp;объективно оценивать профессиональные качества водителей по характеру записи скорости, 
а именно: чем равномернее изменение скорости, тем выше квалификация водителя - он меньше разгоняется и тормозит, реже 
переключает передачи, экономит топливо и ресурс автомобиля в целом; </p>
