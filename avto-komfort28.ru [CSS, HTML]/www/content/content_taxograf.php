<p class="p_style"><strong>НАЗНАЧЕНИЕ:</strong></p>
<p class="p_style">
Контрольные устройства (тахографы) моделей "Штрих - ТахоRUS" исполнений 01-19 
(ТУ 4573-066-52375904-2011) предназначены для контроля режимов труда и отдыха водителей, 
в том числе: регистрации скорости движения, пройденного пути, времени управления транспортным 
средством, времени нахождения на рабочем месте и времени других работ, времени перерывов в 
работе и отдыха водителей.</p>
<p class="p_style">
Контрольные устройства (КУ) включают в себя: устройство обработки данных, блок памяти, часы, работающие 
в реальном масштабе времени, два интерфейса для карт со встроенной микросхемой (для водителя и второго 
водителя), печатающее устройство, дисплей, систему визуального предупреждения, калибровочный/загрузочный 
разъем и устройство ввода данных пользователем. В зависимости от исполнения контрольные устройства "Штрих - 
ТахоRUS" могут быть подключены к датчику скорости, имеющего криптографическую защиту информации, штатному 
импульсному датчику скорости, АBS автотранспортного средства и включать в себя: приемник ГЛОНАСС/ GPS, 
акселерометр, модемы GPRS / GSM , Bluеtооth.</p>
