        <h2>Зачем нужен ландшафтный проект?</h2>
        
        <p class="mb30">Благоустройство ландшафта, равно как и строительство дома, всегда начинается с проекта. Ландшафтный проект помогает заранее учесть все важные детали Вашего участка, особенно если речь идет об участке со сложным рельефом. Предварительное составление проекта поможет избежать ошибок при реализации ландшафтных идей, таких как неправильная посадка, неприживаемость растений, заболачивание участка при неправильной прокладке дренажной системы, несовместимость деревьев друг с другом, несоответствие растений по требованиям к почве, влаге и освещенности. Проектирование происходит в несколько этапов.</p>
        
        <h2 class="lowmargin">Этапы проектирования:</h2>
        
        <ol class="custom-ordered-list">
          <li data-list-nth="1">
            <h4 class="nomargin">Задание на проектирование.</h4>
            Дизайнер перед составлением проекта выслушивает Ваши пожелания относительно будущего вида участка, расположения функциональных зон, декоративных элементов озеленения - и составляет для себя задание на проектирование. 
          </li>
          <li data-list-nth="2">
            <h4 class="nomargin">Анализ участка.</h4>
            Далее дизайнер делает замеры Вашего участка с привязкой зданий, составляет перечень существующих зеленых насаждений, изучает состав почвы, анализирует уклон рельефа и расположение подземных коммуникаций. Без этих данных невозможно начинать ни проектные, ни посадочные работы.
          </li>
          <li data-list-nth="3">
            <h4 class="nomargin">Эскизный проект.</h4>
            Теперь появляется эскизный проект, по которому Вы сможете представить себе будущий вид участка, его композицию, вид дорожек и функциональных зон.  Дизайнер подбирает растения в соответствии с климатическими условиями, расположением грунтовых вод и освещенности участка.
          </li>
          <li data-list-nth="4">
            <h4 class="nomargin">Рабочий проект.</h4>
            На основе эскизного проекта составляется окончательный вариант дизайн-проекта с генеральным планом участка, дендропланом, разбивочно-посадочными чертежами, схемами дорожных покрытий и светильников. По этому проекту в дальнейшем делается его реализация.
          </li>
        </ol>
        
        <p class="mb30">Создание ландшафтного проекта – это профессиональный подход к благоустройству Вашего участка, а также гарантия на работы, свежие творческие идеи и их быстрая реализация. Доверьте Ваш участок профессионалам!</p>
        
