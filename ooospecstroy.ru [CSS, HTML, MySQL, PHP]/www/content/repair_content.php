<a href='javascript:;' onclick=showTooltip2_7() style="text-decoration:underline; color:#666666"><span id='text2_7'>(Развернуть)</span></a>
</h1>
<div id=tooltip2_7 style='display: none'><!--раскрывающийся блок 2_7-->
<p class="myriad_pro12">Нанесение материала производится с помощью нержавеющей стальной кельмы, 
толщиной как можно ближе к размеру зерна. Сразу или спустя некоторое время, в зависимости 
от микроклиматических условий и от впитываемости основания, заглаживается круговыми движениями 
с помощью пластиковой терки, для достижения как можно более равномерного и заполненного вида.</br>
В тех случаях, когда для отделки нужно более чем одно ведро материала, во избежание появления пятен 
из-за возможной разницы в цветовых оттенках, материал нужно эгализировать. То есть материал для 
работы берется из емкости, в которую влили и перемешали  сразу несколько ведер, по мере расхода в 
емкость заливается следующее ведро материала. Таким образом, цвет декоративной штукатурки остается 
ровным по всей наносимой поверхности.</br>
Нанесение ТЕРРАКОАТ САХАРА, ГРАНУЛ следует производить без остановок, от одного края поверхности 
до другого. На поверхности в несколько этажей, штукатурку нужно наносить одновременно на всех
этажах. В этом случае нанесение начинаем с верхнего этажа. Большие площади рекомендуется разделять 
соответствующими по ширине желобами, окантовками и другими способами на небольшие поверхности. 
ВНИМАНИЕ! Из-за неправильно подготовленного основания, проведения работ в неподходящих погодных 
условиях, пренебрежения  правилами эгализации, поверхность может пойти пятнами, разводами или 
привести к несоответствию однородности цвета по всей поверхности. </br>
После работы вымойте инструменты и оборудование водой.</p>
