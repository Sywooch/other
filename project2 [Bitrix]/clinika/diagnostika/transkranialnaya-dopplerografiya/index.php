  <h4 style="text-align: justify;">ОПИСАНИЕ МЕТОДА</h4>
 
  <p class="NEW" style="text-align: justify;">Транскраниальная допплерография - это функциональный метод исследования мозгового кровотока, основанный на эффекте Допплера ( изменения частоты ультразвука при отражении от движущихся элементов крови). Обычно транскраниальную допплерографию назначают больным с сосудистыми заболеваниями головного мозга, дистоническими состояниями, обмороками, головокружениями, при нарушениях мозгового кровообращения.</p>
 
  <p class="NEW" style="text-align: justify;">Особой подготовки к исследованию не требуется, кроме исключения сосудистых препаратов непосредственно перед процедурой. Нельзя также курить, употреблять алкоголь.</p>
 
  <p class="NEW" style="text-align: justify;">Во время исследования Вы лежите на спине на специальной кушетке. Врач сидит за Вашей головой и, касаясь определенных точек на шее и голове ультразвуковым датчиком, лоцирует кровоток. Во время обследования Вам необходимо выполнять команды врача - по команде задержать дыхание, часто подышать, повернуть голову (когда необходимо).</p>
 
  <p class="NEW" style="text-align: justify;">Во время процедуры нельзя разговаривать, вертеть головой. Проведение обследования никаких неприятных ощущений не вызывает. Единственное, что может доставить небольшое неудобство - кратковременной нажатие на область шеи (на 3-4 сек.), которое врач должен выполнять несколько раз во время обследования, чтобы оценить проходимость сосудов.</p>
 
