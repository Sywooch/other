  <h4>ПОКАЗАНИЯ К ИССЛЕДОВАНИЮ</h4>
 
  <p class="NEW">Если Вас или вашего ребенка беспокоят головные боли, головокружения, шум в голове или в ушах, если снизилась память, внимание, способность концентрироваться, если по утрам Вы просыпаетесь с &quot;тяжелой&quot; головой, если появились беспричинные состояния беспокойства, тревоги, мы рекомендуем Вам пройти обследование с использованием ультразвуковой методики для оценки кровообращения головного мозга - транскраниальной ультразвуковой допплерографии.</p>
 
  <p class="NEW">Транскраниальная допплерография используется при таких состояниях, как:</p>
 
  <ul style="list-style: url(/images/box.png);"> 
    <li>мигрень</li>
   
    <li>различные виды головных болей</li>
   
    <li>головокружения</li>
   
    <li>при клинических признаках нарушения мозгового кровообращения.</li>
   
    <li>шум в голове или в ушах</li>
   
    <li>последствия черепно-мозговых травм</li>
   
    <li>повышенная зависимость от изменений погоды</li>
   
    <li>при плохой адаптации к внешним нагрузкам (укачивание в транспорте, метеозависимость и т.д.).</li>
   </ul>
 
  <p></p>
 
  <p class="NEW">Если Вы перенесли инсульт, то допплерографию необходимо провести для прогнозирования степени восстановления нарушений, развития осложнений и рецидивов заболевания.</p>
 
  <p class="NEW">У детей показаниями для исследования являются:</p>
 
  <ul style="list-style: url(/images/box.png);"> 
    <li>задержка развития речи,</li>
   
    <li>неусидчивость, расторможенное поведение, утомляемость</li>
   
    <li>у дошкольников - проявления астенических состояний с повышенной утомляемостью, снижением памяти и внимания.</li>
   </ul>
 
  <p></p>
 
  <p class="NEW">В чем же заключается обследование: с помощью ультразвукового датчика измеряется скорость кровотока по всем сосудам головы и шеи, проводятся различные виды нагрузок, при этом сам обследуемый лежит на удобной кушетке, не испытывая никаких неприятных ощущений. Метод абсолютно безболезненный и не имеет противопоказаний, что делает возможным проведение этой процедуры маленьким детям.</p>
 
  <p class="NEW">По результатам обследования можно с уверенностью сказать где, в каком именно участке головы нарушен кровоток, что, в свою очередь, дает возможность предложить наиболее подходящий курс лечения.</p>
