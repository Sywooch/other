<div class="DIV_CONTENT_PAGE" id="PAGE_558_2" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
  <h4 style="text-align: justify;">ВИДЫ ИССЛЕДОВАНИЙ</h4>
 
  <p class="NEW" style="text-align: justify;">В кабинете КТ НИПНИ им. В.М. Бехтерева выполняются следующие исследования:</p>
 
  <ul style="list-style: url(/images/box.png);"> 
    <li>
      <div style="text-align: justify;">МСКТ головного мозга  </div>
    
      <div style="text-align: justify;">КТ-головного мозга без контрастного усиления проводится при черепно-мозговой травме и является единственным и безальтернативным методом позволяющим в кратчайшие сроки 5-10минут обследовать пациента и получить полную информацию о состоянии костей черепа и головном мозге, проводить динамическое наблюдение и оценивать последствия травмы.  </div>
    
      <div style="text-align: justify;">КТ-головного мозга с контрастным усилением проводится при подозрении на опухоль головного мозга, сосудистые мальформации и аневризмы.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ придаточных пазух носа  </div>
    
      <div style="text-align: justify;">КТ-носа и околоносовых пазух используется для диагностики переломов костей лицевого черепа, опухолей и полипозно-воспалительных изменений придаточных пазух носа.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ орбит  </div>
    
      <div style="text-align: justify;">КТ-орбит выполняется для оценки стенок орбит и позволяет визуализировать глазное яблоко, зрительный нерв, прямые мышцы глаза,жировую клетчатку.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ височных костей  </div>
    
      <div style="text-align: justify;">КТ-височной кости выполняется с целью диагностики при снижении слуха, головокружениях, патологии органа равновесия</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ органов грудной полости  </div>
    
      <div style="text-align: justify;">КТ-легких самый точный и быстрый метод диагностики заболеваний органов грудной клетки (трахеи, бронхов, легких, средостения) – рака легких, пневмонии, туберкулеза.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ органов брюшной полости  </div>
    
      <div style="text-align: justify;">КТ-брюшной полости проводится для поиска и дифференциальной диагностики патологии печени, желчного пузыря, поджелудочной железы, почек, мочеточников.</div>
    </li>
   
    <li style="text-align: justify;">МСКТ надпочечников, почек и мочевыводящих путей</li>
   
    <li>
      <div style="text-align: justify;">МСКТ органов малого таза  </div>
    
      <div style="text-align: justify;">КТ-малого таза применяется для поиска и дифференциальной диагностики патологии органов малого таза и мочевого пузыря.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ шейного, грудного, пояснично-крестцового отделов позвоночника  </div>
    
      <div style="text-align: justify;">КТ-позвоночника выполняется для оценки состояния позвонков и межпозвонковых дисков. Информативность КТ при остеохондрозе, спондилезе и иной патологии тел и отростков позвонков, межпозвоночных дисков, связок позвоночного столба несколько выше, чем МРТ.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ костей и суставов  </div>
    
      <div style="text-align: justify;">КТ-костей верхней и нижней конечностей наиболее информативен для диагностики переломов, их осложнений и исходов, опухолей и воспалительных процессов в костях.  </div>
    
      <div style="text-align: justify;">КТ-суставов выполняется для определения патологических процессов в костях, составляющих сустав и окружающих его тканей.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ мягких тканей  </div>
    
      <div style="text-align: justify;">КТ-мягких тканей шеи выполняется для диагностики образований данной области,.определения степени инвазии опухолью стенки крупных сосудов, для оценки состояния лимфатических узлов шеи.</div>
    </li>
   
    <li>
      <div style="text-align: justify;">МСКТ-ангиография аорты, артерий головного мозга и шеи.  </div>
    
      <div style="text-align: justify;">КТ-ангиография позволяет получить послойную серию изображений кровеносных сосудов; на основе полученных данных посредством компьютерной постобработки с 3D-реконструкцией строится трёхмерная модель кровеносной системы.</div>
    </li>
   
    <li style="text-align: justify;">МСКТ-коронарография - другой метод исследования, рентгеноконтрастный. Он признан самым достоверным и наиболее точным способом диагностирования заболеваний. Врачи могут с его помощью очень точно понять характер сужения коронарной артерии, место и насколько она сужена. Коронарографию называют &quot;золотым стандартом&quot;, когда нужно определить признаки ишемической болезни сердца.</li>
   </ul>
 
  <p></p>
 
  <p class="NEW" style="text-align: justify;">Как правило, КТ-исследования не требуют специальной подготовки.</p>
 
  <p class="NEW" style="text-align: justify;">Однако, КТ органов брюшной полости, также как и любое исследование с введением контрастного препарата желательно делать на пустой желудок.</p>
 
  <p class="NEW" style="text-align: justify;">Если планируется КТ брюшной полости или малого таза, то с вечера до исследования рекомендуется воздержаться от тяжелой пищи, при исследовании кишечника рекомендуется очистительная клизма. Режим подготовки совпадает с таковым для колоноскопии.</p>
 
  <p class="NEW" style="text-align: justify;">Если Вам и Вашим близким требуется компьютерная томография, мы готовы выполнить исследование быстро, оперативно, согласно самым современным методикам.</p>
 
