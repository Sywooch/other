<div class="DIV_CONTENT_PAGE" id="PAGE_558_1" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
  <h4 style="text-align: justify;">ОПИСАНИЕ МЕТОДА</h4>
 
  <p class="NEW" style="text-align: justify;">Компьютерная томография (КТ) один из методов диагностики, который произвёл революцию не только в рентгенологии, но и в медицине в целом. Это метод, который позволяет увидеть органы и ткани, которые не видны при рентгенографии, такие как печень, почки, селезенка, поджелудочная железа, а также самые мелкие изменения в легких, пазухах, костной системе. Также преимуществом КТ является возможность визуализации сосудов и сердца.</p>
 
  <p class="NEW" style="text-align: justify;">Компьютерная томография (КТ, МСКТ) использует рентгеновские лучи для получения послойных изображений и детальной визуализации структуры тканей практически всех внутренних органов человека. Получение изображения при КТ основано на различной плотности органов и тканей, через которые проходят рентгеновские лучи. Эти малейшие изменения улавливаются и фиксируются датчиками, информация от которых обрабатывается компьютером. При проведении исследования рентгеновская трубка с узким пучком рентгеновских лучей вращается вокруг пациента и передает данные на экран компьютера в виде последовательных продольных и поперечных &laquo;срезов&raquo;. Ведь дословно томография и переводится, как изображение среза. Чем тоньше «срез», тем точнее диагностика. Современные томографы позволяют видеть детали менее миллиметра. В некоторых случаях &ndash; это спасение жизни. И хотя в основе метода компьютерной томографии используется рентгеновское излучение, доза облучения пациента при данном исследовании значительно ниже, чем при рентгенографии, а информативность выше во много раз. Самые современные КТ позволяют значительно уменьшить продолжительность исследования, получить сотни тонких срезов (до 0,5 мм) с минимальным облучением пациента. Именно такой компьютерный томограф (PHILIPS BRILLIANCE 64) установлен в институте им. В.М.Бехтерева, это мультиспиральный компьютерный томограф последнего поколения «Philips Brilliance» с 64 рядами детекторов, обладающий уникальными характеристиками, которые позволяют выполнять высокоточные исследования с возможностью производить многоплоскостные реконструкции изображений и получать трехмерные объемные изображения. Благодаря высокоточным трехмерным проекциям, которые дают возможность исследовать положение, форму, размеры и строение различных органов и структур организма, компьютерная томография является важнейшим инструментом современной диагностики различных заболеваний.</p>
 
  <p class="NEW" style="text-align: justify;">Сканирование выполняется в течение нескольких секунд, что особенно важно при обследовании пациентов с тяжелым общим состоянием, детей, пациентов с клаустрофобией.</p>
 
  <p class="NEW" style="text-align: justify;">В большинстве случаев КТ выполняется в плановом порядке, по направлению врача, для окончательного подтверждения диагноза. Как правило, перед проведением компьютерной томографии, выполняются более простые исследования &mdash; рентген, УЗИ, анализы и т. д.</p>
 
  <p class="NEW" style="text-align: justify;">Диагностические возможности его огромны. Новые программы позволяют проводить исследования, о которых раньше приходилось только мечтать. Учтены недостатки предыдущих аппаратов. У людей с боязнью замкнутого пространства – клаустрофобией, исчезает беспокойство. Освещение, вентиляция создают спокойную и комфортную обстановку для пациента. Увеличена еще одна возможность томографа - в нем может поместиться человек весом до 150 кг. Значительно сократилось и время исследования. По желанию пациента, данные могут быть записаны на компьютерный диск, что имеет свои преимущества и удобства.</p>
 
  <p class="NEW" style="text-align: justify;">Еще одно преимущество этого метода – исследование пациентов с кардиостимуляторами, металлическими клипсами, конструкциями после операций и искусственными сердечными клапанами. Именно такие пациенты не могли обследоваться на МРТ.</p>
 
  <p class="NEW" style="text-align: justify;">Однако использование даже самого современного томографа не является залогом точной диагностики. Постановка правильного диагноза подразумевает под собой от врача - радиолога глубочайшие знания и накопленный опыт. Сотрудники отделения НИПНИ им. Бехтерева обладают всеми вышеперечисленными качествами.</p>
 
