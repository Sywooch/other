  <h4 style="text-align: justify;">ОПИСАНИЕ МЕТОДА</h4>
 
  <p class="NEW" style="text-align: justify;">Дуплексное сканирование (ДС) - высокоинформативный метод объединяющий в себе возможности УЗИ и допплеровского спектрального исследования.</p>
 
  <p style="text-align: justify;">С помощью дуплексного сканирования можно исследовать АРТЕРИИ И ВЕНЫ человеческого организма:</p>
 
  <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
    <li style="text-align: justify;">Головного мозга</li>
   
    <li style="text-align: justify;">Шеи</li>
   
    <li style="text-align: justify;">Брюшной полости</li>
   
    <li style="text-align: justify;">Конечностей</li>
   </ul>
 
  <p></p>
 
  <p class="NEW" style="text-align: justify;">Данный метод позволяет визуализировать исследуемые сосуды в режиме реального масштаба времени и одновременно определить скорости кровотока.</p>
 
  <p class="NEW" style="text-align: justify;">При дуплексном сканировании статические ткани в области исследования представляются оттенками серой шкалы, а движущаяся кровь в сосудах окрашивается в красный или синий цвет в зависимости от направления кровотока - что способствует распознаванию патологических изменений стенок и просвета сосудов, появляется возможность измерить диаметр сосуда, оценить ход сосуда и достоверно провести расчет о количественных параметрах кровотока.</p>
 
  <p class="NEW" style="text-align: justify;">Во время исследования пациент находится в положении лежа на спине, голова - на низкой подушке с легким поворотом в сторону, противоположную исследуемому сосуду. Метод безболезненный, без вредных воздействий для организма, специальной подготовки для проведения исследования не требуется.</p>
 
