<h1 style="text-align: justify;">МРТ ВСЕГО ТЕЛА</h1>
 
<p style="text-align: justify;">Магнитно-резонансная томография (МРТ) всего тела во фронтальной плоскости дает в настоящее время возможность исследовать тело человека.</p>
 
<p style="text-align: justify;">Современные МР томографы в настоящее время оснащены набором катушек и программ, позволяющих провести обследование всего тела человека за одно исследование. Это позволяет сразу оценить состояние очень многих органов и тканей человеческого тела, начиная от головного мозга и заканчивая конечностями.</p>
 
<p style="text-align: justify;">Исследование занимает от 30 минут до 1 часа и может включать 2 зоны (позволяя одновременно исследовать головной мозг, шею, грудную клетку, частично брюшную полость), либо 3 зоны (от макушки головы до голеностопных суставов).</p>
 
<p style="text-align: justify;">Эта программа является скрининговой для поиска метастазов и во многом аналогична ПЭТ сканированию.</p>
 
