<h4 style="text-align: justify;">ЭЛЕКТРОЭНЦЕФАЛОГРАФИЯ (ЭЭГ)</h4>
 
<p class="NEW" style="text-align: justify;">ЭЭГ - метод регистрации электрической активности головного мозга через неповрежденные покровы головы, позволяющий судить о его физиологической зрелости, функциональном состоянии, наличии локальных и очаговых поражений, общемозговых расстройств и их характере.</p>
 
<p class="NEW" style="text-align: justify;">Основоположник клинической электроэнцефалографии (1928 год) австрийский психиатр Ганс Бергер. На протяжении уже без малого ста лет ЭЭГ является единственным методом функциональной диагностики головного мозга, широко применяемым в неврологии, психиатрии, нейрохирургии, реабилитологии, реанимационной практике.</p>
 
<p class="NEW" style="text-align: justify;">ЭЭГ позволяет:</p>
 
<ul style="list-style: url(/images/box.png); text-align: justify;"> 
  <li style="margin: 0.6em 0px;">оценить общее функциональное состояние головного мозга с учётом индивидуальных особенностей конкретного пациента;</li>
 
  <li style="margin: 0.6em 0px;">выявить наличие и характер нарушений в его работе;</li>
 
  <li style="margin: 0.6em 0px;">определить локальные и очаговые повреждения и в ряде случаев установить их природу;</li>
 
  <li style="margin: 0.6em 0px;">определять характер и объём применения как лекарственных препаратов, так и других лечебных процедур;</li>
 
  <li style="margin: 0.6em 0px;">уточнить показания к применению тех или иных дополнительных исследований (МРТ, УЗДГ), тех или иных лечебных воздействий и процедур, а также &ndash; хирургических вмешательств.</li>
 </ul>
 
<p class="NEW" style="font-family: Verdana, Arial, Helvetica, sans-serif; font-size: 13px; text-align: -webkit-center; background-color: rgb(213, 213, 213);"></p>
 
<p class="NEW" style="text-align: justify;">ЭЭГ-контроль при операциях на головном мозге (нейрохирургия) позволяет:</p>
 
<ul style="list-style: url(/images/box.png); text-align: justify;"> 
  <li style="margin: 0.6em 0px;">дозировать глубину наркоза;</li>
 
  <li style="margin: 0.6em 0px;">контролировать безопасность вмешательства;</li>
 
  <li style="margin: 0.6em 0px;">точно определять границы повреждения участков мозга,</li>
 
  <li style="margin: 0.6em 0px;">минимизировать объём вмешательства;</li>
 </ul>
 
<p class="NEW" style="font-family: Verdana, Arial, Helvetica, sans-serif; font-size: 13px; text-align: -webkit-center; background-color: rgb(213, 213, 213);"></p>
 
<p class="NEW" style="text-align: justify;">Будучи неспецифическим методом диагностики, ЭЭГ приобретает особую ценность при динамическом наблюдении, позволяя определять:</p>
 
<ul style="list-style: url(/images/box.png); text-align: justify;"> 
  <li style="margin: 0.6em 0px;">тенденции и динамику развития патологического процесса;</li>
 
  <li style="margin: 0.6em 0px;">влияние и эффективность проводимой терапии;</li>
 
  <li style="margin: 0.6em 0px;">целесообразность применения тех или иных методов лечения;</li>
 
  <li style="margin: 0.6em 0px;">оптимальный объём лекарственной нагрузки и т. д.</li>
 </ul>
 
<p class="NEW" style="font-family: Verdana, Arial, Helvetica, sans-serif; font-size: 13px; text-align: -webkit-center; background-color: rgb(213, 213, 213);"></p>
 
<p class="NEW"> </p>
 
<div style="text-align: justify;">Особую значимость ЭЭГ приобрела в изучении эпилепсии и разработке методов её лечения.   </div>
 
<div style="text-align: justify;">И по сей день ЭЭГ остается, по сути, единственным методом объективной диагностики этого распространённого заболевания, позволяющим:</div>
 
<p></p>
 
<p style="text-align: -webkit-center;"></p>
 
<ul style="list-style: url(/images/box.png); text-align: justify;"> 
  <li style="margin: 0.6em 0px;">провести дифференциальную диагностику с другими пароксизмальными состояниями;</li>
 
  <li style="margin: 0.6em 0px;">определить наличие, локализацию и характер эпилептического очага;</li>
 
  <li style="margin: 0.6em 0px;">спрогнозировать дальнейшее развитие эпилептического процесса;</li>
 
  <li style="margin: 0.6em 0px;">подобрать наиболее эффективные лекарственные препараты и их дозы;</li>
 
  <li style="margin: 0.6em 0px;">выработать оптимальную схему и режим лечения и наблюдения;</li>
 
  <li style="margin: 0.6em 0px;">оценить в динамике эффективность лечения;</li>
 
  <li style="margin: 0.6em 0px;">предложить схему отмены лекарств при достижении длительной ремиссии.</li>
 </ul>
 
<p style="text-align: justify;"></p>
 
<p style="text-align: justify;">Показания к проведению электроэнцефалографии (ЭЭГ):</p>
 
<ul style="list-style: url(/images/box.png); text-align: justify;"> 
  <li style="margin: 0.6em 0px;">Все виды эпилептических припадков;</li>
 
  <li style="margin: 0.6em 0px;">Обмороки;</li>
 
  <li style="margin: 0.6em 0px;">Любые приступообразные и периодически повторяющиеся патологические состояния;</li>
 
  <li style="margin: 0.6em 0px;">Пароксизмальные нарушения во сне - ночные страхи, снохождения, головные боли, пароксизмальные боли в животе и т. п.;</li>
 
  <li style="margin: 0.6em 0px;">Все виды неврологических расстройств;</li>
 
  <li style="margin: 0.6em 0px;">Инсульты и их последствия;</li>
 
  <li style="margin: 0.6em 0px;">Черепно-мозговые травмы любой степени тяжести и их последствия;</li>
 
  <li style="margin: 0.6em 0px;">Психические расстройства, в особенности – на фоне лечения препаратами нового поколения;</li>
 
  <li style="margin: 0.6em 0px;">Невротические проявления и навязчивости (навязчивые движения, тики, заикание, энурез);</li>
 
