<h4 style="text-align: justify;">ЭЭГ-МОНИТОРИНГ</h4>
 
<p class="NEW" style="text-align: justify;">Особым видом ЭЭГ-исследования является мониторинг</p>
 
<p class="NEW" style="text-align: justify;">Видеомониторинг ЭЭГ - это одновременная регистрация электроэнцефалографии и видеозаписи поведения пациента в состоянии бодрствования и сна в течение длительного времени (от 3 до 24 часов непрерывной записи).</p>
 
<p class="NEW" style="text-align: justify;">Это дает возможность выявить редкие проявления патологических изменений биоэлектрической активности мозга, не выявляемые при обычной ЭЭГ. А синхронизация электроэнцефалограммы и видеозаписи позволяет сопоставить изменения на ЭЭГ и состояние пациента (его поведение, сознание, мимику, тонкую моторику и т.д.) в любой момент времени.</p>
 
<p class="NEW" style="text-align: justify;">Метод видеомониторинга ЭЭГ имеет большое значение при отмене или снижении дозы противоэпилептических препаратов и для прогноза риска возникновения рецидивов.</p>
 
<p class="NEW" style="text-align: justify;">Перед снижением дозы и в течение 1 года после полной отмены препаратов рекомендуется проведение видеомониторинга ЭЭГ с обязательным включением записи во сне.</p>
 
<div style="text-align: justify;"><hr style="border-top-width: 0px; border-color: rgb(233, 233, 233) rgb(233, 233, 233) rgb(170, 170, 170); font-size: 11px; line-height: normal; color: rgb(170, 170, 170); border-right-width: 0px; border-left-width: 0px; border-bottom-style: solid; font-family: Verdana; text-align: justify; background-color: rgb(245, 240, 234);" /></div>
 
<p class="NEW" style="text-align: justify;">Помимо классической ЭЭГ существенную помощь в диагностике поражений нервной системы оказывают такие современные нейрофизиологические методики, как электронейромиография и регистрация вызванных потенциалов.</p>
 
<p class="NEW" style="text-align: justify;">Особый интерес в последние годы приобретает поиск и разработка клинически значимых методов математической обработки ЭЭГ-данных.</p>
 
