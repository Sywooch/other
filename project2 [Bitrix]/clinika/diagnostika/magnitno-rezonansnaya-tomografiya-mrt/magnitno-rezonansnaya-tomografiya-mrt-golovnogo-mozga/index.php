    <h4>ПОКАЗАНИЯ К МРТ ГОЛОВЫ</h4>
   
    <p class="NEW">Неотложные состояния:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>Черепно-мозговая травма (после КТ).</li>
     
      <li>Острое нарушение мозгового кровообращения.</li>
     
      <li>Экстренные ситуации у послеоперационных больных.</li>
     </ul>
   
    <p></p>
   
    <p class="NEW">Плановые исследования:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>Подозрение на опухоль головного мозга или его оболочек.</li>
     
      <li>Диагностика дегенеративных или демиелинизирующих заболеваний.</li>
     
      <li>Диагностика очаговых воспалительных процессов головного мозга.</li>
     
      <li>Выявление артериовенозных аневризм головного мозга.</li>
     
      <li>Оценка состояния ликворопроводящих путей и ликворосодержащих структур головного мозга (кисты, гигромы, порэнцефалия).</li>
     
      <li>Выявление аномалий развития мозга.</li>
     
      <li>Оценка состояния головного мозга после перенесенного нарушения мозгового кровообращения.</li>
     
      <li>Оценка состояния головного мозга после черепно-мозговой травмы.</li>
     
      <li>Оценка результатов оперативного лечения заболеваний головного мозга. Выявление заболеваний гипофиза.</li>
     
      <li>Лицевой череп: объемные поражения орбиты, глазных яблок, объемные поражения придаточных пазух, носоглотки.</li>
     </ul>
   
