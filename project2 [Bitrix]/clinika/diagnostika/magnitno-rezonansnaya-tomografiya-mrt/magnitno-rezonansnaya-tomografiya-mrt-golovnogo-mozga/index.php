    <h4>ОПИСАНИЕ МЕТОДА</h4>
   
    <p class="NEW">МРТ головного мозга помогает получить детализированное изображение головного мозга, менингеальной оболочки, внутреннего уха, глаз и нервной ткани в разных проекциях и без помех, создаваемых прилегающей костной тканью.</p>
   
    <p class="NEW">Магнитно-резонансная томография показана при неврологических расстройствах, так как в состоянии четко показать различные типы нервной ткани. На снимке ясно видны ствол головного мозга и задняя часть полушарий, чего трудно добиться при КТ-сканировании. МРТ очень помогает при диагностировании демиелинизирующих заболеваний. К ним относится рассеянный склероз, при котором деструкции подвергается миелиновое покрытие нервов.</p>
   
    <p class="NEW">МРТ различает опухоли и другие поражения белого и серого веществ головного мозга на фоне нормальной ткани.</p>
   
    <p class="NEW">Если у Вас возникла симптоматика, описанная ниже, Вам необходимо срочно обратиться к невропатологу, а при отсутствии такой возможности обязательно сделайте МРТ!</p>
   
    <p class="NEW">МРТ надо обязательно сделать при внезапно возникшей или постепенно нарастающей неврологической симптоматике, как, например:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>головокружение</li>
     
      <li>появление слабости в руке или ноге</li>
     
      <li>двоение в глазах</li>
     
      <li>сильные и продолжительные головные боли</li>
     
      <li>при возникновении судорог или нарастании частоты судорожных приступов</li>
     
      <li>снижение памяти и ориентировки в окружающей обстановке</li>
     </ul>
   
    <p></p>
   
    <p class="NEW">Обязательно пройдите МРТ или КТ после ЧМТ, сопровождавшейся потерей сознания или появлением какой-либо неврологической симптоматики (слабость в руке или ноге, рвота, двоение в глазах, сильная головная боль и т.д.)</p>
   
