    <h4>ОПИСАНИЕ МЕТОДА</h4>
   
    <p class="NEW">Магнитно-резонансная томография (МРТ) позвоночника является неинвазивной процедурой, которая использует мощное магнитное поле и радиоволны для получения снимков области позвоночника, в том числе - отдельных позвонков, спинного мозга, и пространства между позвонками, через которое проходит путь нервов.</p>
   
    <p class="NEW">На данный момент MРТ - это наиболее точный метод получения снимков позвоночника, известный в медицинской практике.</p>
   
    <p class="NEW">МРТ может показать точное место нахождения опухолей или других патологических процессов, связанных с болезнями позвоночника, спинного мозга, или межпозвонковых дисков. Метод позволяет получить детализированные изображения труднодоступных для просмотра областей позвоночника и спинного мозга, в том числе: спинного мозга, костного мозга и мягких тканей.</p>
   
    <p class="NEW">С помощью МРТ позвоночника:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>Можно получить четкую картину анатомии позвоночника</li>
     
      <li>Визуально увидеть измененные или пораженные болезнью ткани позвоночника</li>
     
      <li>Обезопасить хирургическое вмешательство на позвоночнике в случаях защемления нерва или смещения позвонков</li>
     
      <li>Доктора наблюдают за изменениями в позвоночнике после операции, чтобы вовремя предотвратить развитие инфекции, или появление рубцовой ткани</li>
     
      <li>Диагностируют возможные причины болей в спине (например, перелом позвонка в результате сдавления, повреждение или дегенерация межпозвонковых дисков)</li>
     </ul>
   
    <p></p>
   
    <p class="NEW">В зависимости от локализации симптомов, только определенная часть позвоночника может подвергнуться обследованию: шейный, грудной или поясничный его отделы.</p>
   
