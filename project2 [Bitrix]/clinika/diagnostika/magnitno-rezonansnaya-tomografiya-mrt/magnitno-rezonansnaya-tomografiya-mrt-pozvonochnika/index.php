    <p class="NEW">Показания для магнитно-резонансной томографии позвоночника, костей, суставов, мышц:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>Подозрение на метастазы или первичные опухоли костей, позвонков, мышц, остеомиелитические поражения костей</li>
     
      <li>Дегенеративно-дистрофические, травматические поражения тел позвонков и межпозвонковых дисков.</li>
     </ul>
   
    <p></p>
   
    <p class="NEW">Показания для магнитно-резонансной томографии спинного мозга:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li>Подозрение на очаговое поражение, опухоли спинного мозга или его оболочек.</li>
     
      <li>Оценка ликворных пространств спинного мозга, выявление сирингомиелии.</li>
     
      <li>Оценка результатов оперативных вмешательств на спинном мозге.</li>
     
      <li>Подозрение на патологию спинного мозга вертеброгенного происхождения (травму, дегенеративные или опухолевые поражения позвоночника).</li>
     </ul>
   
