<h1><span style="text-align: justify;">Магнитно-резонансная томография (МРТ)</span></h1>
 
<p style="text-align: justify;">Магнитно-резонансная томография обладает высокой чувствительностью, информативностью и специфичностью, поэтому этот метод применяется для диагностики заболеваний или травматических повреждений суставов.</p>
 
<p style="text-align: justify;">МРТ позволяет увидеть кортикальный слой кости, сухожилия, связочный аппарат, хрящевые элементы, синовиальную оболочку, сосуды и нервы.</p>
 
<p style="text-align: justify;">Применение этого диагностического метода подходит для оценки морфологического строения хрящей, связок, суставных сумок, окружающих мышц.</p>
 
<p style="text-align: justify;">Общие показания к проведению МРТ суставов и опорно-двигательного аппарата</p>
 
