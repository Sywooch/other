  <div class="DIV_CONTENT_PAGE" id="PAGE_515_8" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ЗАПОЛНЕНИЕ И ПРОГРАММИРОВАНИЕ ПОМПЫ</font></h4>
   
    <p style="text-align: justify;">Очень важно регулярно посещать врача для заполнения или программирования системы. Несоблюдение графика заполнения помпы может привести к полному расходу лекарства, т.е. к появлению симптомов спастики. График посещений определяет лечащий врач, а их периодичность будет определяться количеством вводимого лекарства и, в целом, Вашим состоянием здоровья. Периодичность таких посещений обычно составляет 2-3 месяца, максимум перерыв между визитами к врачу 6 месяцев.</p>
   
    <p style="text-align: justify;">Заполнение помпы проводится чрезкожно без анестезии.</p>
   
    <p style="text-align: justify;">Требуемые изменения и контроль работы помпы проводятся путем ее программирования, которое проводится телеметрически, то есть при помощи радиосигналов. Во время сеанса телеметрии (врач располагает телеметрическое устройство над кожей в месте нахождения помпы) проверяется работоспособность помпы, при необходимости изменяется количество или график подачи лекарства в организм.</p>
   
    <div style="text-align: justify;"> 
      <br />
     </div>
   </div>
 
  <div class="DIV_CONTENT_PAGE" id="PAGE_515_9" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">РЕКОМЕНДАЦИИ ДЛЯ ПАЦИЕНТОВ</font></h4>
   
    <p style="text-align: justify;">Хотя помпа действует в полностью автоматическом режиме, следует всегда помнить ряд простых советов:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li style="text-align: justify;">Вы должны расспросить Вашего врача обо всем, что Вам непонятно или неясно.</li>
     
      <li style="text-align: justify;">Вы должны знать номер модели помпы, объем резервуара с лекарством, а также имеется ли блок прямого доступа к катетеру.</li>
     
      <li style="text-align: justify;">Вы должны помнить: какой именно препарат и в какой дозе Вы получаете.</li>
     
      <li style="text-align: justify;">Следуйте указаниям Вашего врача об уменьшении (прекращении) приема иных антиспастических препаратов во избежание превышения дозы лекарств.</li>
     
      <li style="text-align: justify;">Соблюдайте график посещения врача для контроля работы и заполнения помпы.</li>
     
      <li style="text-align: justify;">Избегайте видов деятельности, способных повредить нормальной работе Вашей системы (удары в область расположения помпы, резкие нагрузки - в виде сгибания/разгибания спины и т.п.).</li>
     
      <li style="text-align: justify;">Всегда предупреждайте медицинских специалистов о том, что Вам имплантирована электронная система.</li>
     
      <li> 
        <div style="text-align: justify;">Всегда имейте при себе следующую информацию (карточки):</div>
       
        <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
          <li style="text-align: justify;">информацию для скорой помощи;</li>
         
          <li style="text-align: justify;">идентификационную карточку Вашей системы;</li>
         
          <li style="text-align: justify;">информацию о срочной медицинской помощи в случае превышения дозы лекарства.</li>
         </ul>
       </li>
     
      <li style="text-align: justify;">Если Ваша помпа издает предупреждающие сигналы - срочно свяжитесь с Вашим врачом.</li>
     
      <li style="text-align: justify;">При длительном отъезде из места постоянного проживания - обязательно предупредите об этом Вашего врача для организации очередного заполнения помпы в месте Вашего пребывания или изменения режимов ее работы.</li>
     
      <li style="text-align: justify;">Вы должны быть уверены, что Ваши родственники и близкие друзья знают о том, что Вам имплантирована помпа и способны оказать Вам первую помощь.</li>
     
      <li style="text-align: justify;">Если Вы услышите тихие прерывистые сигналы (несколько раз в минуту), издаваемые помпой - звоните Вашему врачу. Это начала работать система звукового «тревожного» оповещения. Сигналы означают необходимость проведения проверки работы помпы - скорого завершения срока службы батарей, прекращения подачи лекарства и т.п. После проверки работы помпы с помощью программатора врач отключит «тревожный» сигнал и Вы (по согласованию с врачом) можете не включать более режим оповещения помпы, если он по какой-либо причине Вам не удобен.</li>
     </ul>
   
