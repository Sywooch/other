  <div class="DIV_CONTENT_PAGE" id="PAGE_515_5" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ОПИСАНИЕ ПОМПЫ:</font></h4>
   
    <p style="text-align: justify;">Система состоит из помпы и катетера, которые хирургическим путем помещаются (имплантируются) внутри Вашего организма.</p>
   
    <p></p>
   <center style="text-align: justify;"><img src="/content/clinika/nejrohirurgiya/provodimye-operacii/baklofenovaya-pompa/01.png" style="border-color: rgb(0, 0, 0);" width="550" height="272"  /></center> 
    <p></p>
   
    <p style="text-align: justify;">Помпа представляет собой титановый плоский цилиндр высотой 19,5 мм, диаметром 87,5 мм. и весом 185г (для помпы с объемом 20 мл в заполненном состоянии), в котором находится перистальтический насос, электронный модуль и батареи, система звукового оповещения, система антибактериальной микрофильтрации, а также резервуар для лекарства, вводимого в организм через катетер. Существует две модели помпы с внутренним резервуаром объемом 20 или 40 мл.</p>
   
    <p style="text-align: justify;">В центре лицевой поверхности помпы расположено отверстие для заполнения ее лекарством. Оно постоянно закрыто самозатягивающейся мембраной. Для заполнения помпы врач или медсестра вводят иглу шприца, заполненного лекарством, непосредственно через кожу больного в это отверстие.</p>
   
    <p style="text-align: justify;">Катетер - гибкая полимерная трубка малого сечения (диаметр дистального конца 1,4 мм.), располагаемая подкожно и предназначенная для доставки лекарства от помпы в интратекальное пространство.</p>
   
    <p style="text-align: justify;">Существенным элементом функционирования и контроля над деятельностью Вашей системы является компьютерный программатор, который позволяет врачу или медсестре телеметрическим путем (совершенно безболезненно для пациента) задавать условия работы помпы и следить за ходом подачи лекарства.</p>
   
    <div style="text-align: justify;"> 
      <br />
     </div>
   </div>
 
  <div class="DIV_CONTENT_PAGE" id="PAGE_515_6" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ПРИНЦИП РАБОТЫ ПОМПЫ</font></h4>
   
    <p style="text-align: justify;">Помпа создана таким образом, чтобы, в соответствии с рассчитанным Вашим лечащим врачом графиком лечения, совершенно незаметно для Вас доставлять строго определенное количество лекарства в должное время дня или ночи в организм.</p>
   
    <p style="text-align: justify;">У установленной помпы есть электронная «память», хранящая все указания врача для ее успешной работы.</p>
   
    <div style="text-align: justify;"> 
