    <h4 style="text-align: justify;"><font face="Times New Roman">СРОК СЛУЖБЫ</font></h4>
   
    <p style="text-align: justify;">Вашу помпу придется заменить по истечении срока службы батарей. Обычный срок эксплуатации батарей составляет до 4-5 лет в зависимости от интенсивности работы системы.</p>
   
