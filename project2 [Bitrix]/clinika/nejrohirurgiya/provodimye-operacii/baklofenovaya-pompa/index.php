  <div class="DIV_CONTENT_PAGE" id="PAGE_515_1" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ДОЛГОСРОЧНАЯ ИНТРАТЕКАЛЬНАЯ БАКЛОФЕНОВАЯ И МОРФИНОВАЯ ТЕРАПИЯ</font></h4>
   
    <p style="text-align: justify;">Препарат Баклофен (Лиорезал) в таблетированной форме широко используется при лечении спастики. Однако при пероральном приеме часть пациентов либо испытывает ряд побочных эффектов, либо не получает должного эффекта лечения заболевания. &laquo;Лиорезал&raquo; для интратекального введения» или, как его еще называют, «инъекционный баклофен» является жидкой формой баклофена и вводится непосредственно в окружающую спинной мозг интратекальную область позвоночного столба.</p>
   
    <p style="text-align: justify;">Помпа представляет собой насос, который и подает лекарство (баклофен или морфин) прямо в эту область.</p>
   
    <p style="text-align: justify;">Таким образом, Баклофен (Лиорезал) применяют для лечения тяжелого спастического состояния церебрального происхождения у больных, которые нечувствительны к пероральному приему баклофена, либо испытывают болезненные ощущения при использовании эффективных доз препарата, и, когда другие подходы к лечению нежелательны (противопоказаны) или неэффективны.</p>
   
    <p style="text-align: justify;">Начиная с начала 80-х гг. было проведено множество исследований и испытаний данной терапии. В частности, три контрольных клинических исследования были предприняты для выяснения эффективности и безопасности применения метода долгосрочной интратекальной баклофеновой терапии (ДИБТ) для лечения тяжелых спастических состояний церебрального происхождения. Эти исследования проводились в нескольких медицинских центрах и включали изучение последствий введения тест-дозы баклофена (предварительное тестирование) в ходе отбора пациентов, а также результаты дальнейшего лечения пациентов методом ДИБТ.</p>
   
