    <h4 style="text-align: justify;"><font face="Times New Roman">ПОКАЗАНИЯ ДЛЯ БАКЛОФЕНОВОЙ И МОРФИНОВОЙ ПОМПЫ:</font></h4>
   
    <p style="text-align: justify;">Показания:</p>
   
