  <div class="DIV_CONTENT_PAGE" id="PAGE_515_4" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ОЦЕНКА ЭФФЕКТИВНОСТИ ИНТРАТЕКАЛЬНОГО ВВЕДЕНИЯ БАКЛОФЕНА (ЛИОРЕЗАЛА) ДО ОПЕРАЦИИ</font></h4>
   
    <p style="text-align: justify;">Предварительно вводится тест-доза Баклофена (Лиорезала), чтобы выяснить, как организм пациента отвечает на введение препарата. Данная процедура проводится в клинических условиях. Тест-доза Баклофена (Лиорезала) вводится с помощью небольшой иглы в интратекальное пространство ствола спинного мозга в нижней части спины. Сразу же после инъекции иглу вынимают (возможна установка небольшой трубки - катетера - для инфузии лекарства в течение непродолжительного времени). В случае если препарат снимает спазмы или ригидное состояние мышечной системы, можно принять решение об установке помпы на постоянной основе.</p>
   
    <p style="text-align: justify;">* Введение препарата непосредственно в цереброспинальную жидкость позволяет миновать гематоэнцефалический барьер и непосредственно воздействовать на рецепторы спинного мозга. Дозировка лекарств, при этом столь минимальна, что с успехом позволяет избежать многих системных осложнений.</p>
   
    <div style="text-align: justify;"> 
