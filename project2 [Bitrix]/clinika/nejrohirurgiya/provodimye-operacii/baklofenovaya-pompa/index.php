  <div class="DIV_CONTENT_PAGE" id="PAGE_515_7" style="border: 0px solid black; margin: 0px; padding: 0px;"> 
    <h4 style="text-align: justify;"><font face="Times New Roman">ИМПЛАНТАЦИЯ СИСТЕМЫ</font></h4>
   
    <p style="text-align: justify;">Система (помпа и катетер) имплантируется хирургическим путем.</p>
   
    <p style="text-align: justify;">Нейрохирург через небольшой разрез в нижней части спины интратекально вводит катетер. Помпу помещают в подкожный «карман», который делают путем надреза в подреберной области. После подсоединения катетера к помпе врач зашивает «карман».</p>
   
    <div style="text-align: justify;"> 
      <br />
     </div>
