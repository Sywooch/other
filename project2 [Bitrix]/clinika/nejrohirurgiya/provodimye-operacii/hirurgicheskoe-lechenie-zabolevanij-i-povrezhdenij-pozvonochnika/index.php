<h1><font face="Times New Roman" size="5">Хирургическое лечение заболеваний и повреждений позвоночника</font></h1>
 
<p>Хирургическое лечение заболеваний и повреждений позвоночника:</p>
 
<ul style="list-style: url(http://skoraya55.ru/images/box.png); text-align: justify;"> 
  <li>Оперативное лечение остеохондроза (наиболее распространенного дегенеративно-дистрофического заболевания позвоночника) шейного, грудного, пояснично-крестцового отделов позвоночника применяется при наличии следующих осложнений: грыж и протрузий межпозвонковых дисков, спондилолистеа, вторичного стеноза позвоночного канала.  
    <br />
   
    <br />
   Оперативное лечение, в зависимости от медицинских показаний, может быть как &laquo;малоинвазивным&raquo; - это микродекомпрессия (т.е. устранение сдавления спинномозговых корешков, дискэктомия) с использованием различных микрохирургических техник, так и «открытым» - интерламинарное транслигаментарное удаление грыжи диска.  
    <br />
   
    <br />
   По показаниям производится дополнительная динамическая или жесткая фиксация поврежденного сегмента позвоночника. 
    <br />
    </li>
 
  <li>Оперативное лечение при травматических поражениях позвоночника на всех уровнях, в том числе последствий травм позвоночника и комплексное лечение после «неудачных» предшествующих операций.</li>
 
  <li>Оперативное лечение при онкологических заболеваниях с поражением позвоночного столба, спинного мозга, спинномозговых корешков.</li>
 
  <li>Оперативное лечение ряда врожденных аномалий позвоночника.</li>
 </ul>
 
