  <h4 style="text-align: center;"><font face="Times New Roman">ОБ АППАРАТЕ</font></h4>
 
  <p style="text-align: justify;">Созданный фирмой Cyberonics Inc.'s аппарат, официально называемый стимулятор блуждающего нерва (VNS) за счет регулярно посылаемых электрических импульсов, может с успехом применяться более чем у 4 миллионов пациентов, страдающих депрессией и эпилепсией при которых лекарства и открытые оперативные методы лечения не эффективны.</p>
 
  <p style="text-align: justify;">В настоящее время устройство находится в cвободной продаже и применяется для лечения некоторых форм эпилепсии и депрессии.</p>
 
  <p style="text-align: justify;">Генератор импульсов размером с карманные часы имплантируется в подкожную клетчатку грудной клетки, а проводки тянутся по шее к блуждающим нервам, проводя электрические импульсы по этому нерву в определенную зону мозга, которая предположительно отвечает за настроение.</p>
 
