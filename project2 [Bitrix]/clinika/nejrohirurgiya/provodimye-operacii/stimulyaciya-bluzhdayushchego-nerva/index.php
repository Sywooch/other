  <h4 style="text-align: justify;">АНАТОМИЧЕСКАЯ И ФИЗИОЛОГИЧЕСКАЯ ОСНОВА ВАГУСНОЙ СТИМУЛЯЦИИ</h4>
 
  <p style="text-align: justify;">20%-эфферентные волокна (заднее моторное ядро и амбигус-парасимпатическая иннервация+мышцы гортани и глотки), 80%-афферентные волокна (сердце, аорта, легкие, гастроинтестинальный тракт, ядра солитарного тракта, шовное ядро, голубоватое пятно, широкое представительство в стволе, диффузные многочисленные кортикальные контакты).</p>
 
  <p style="text-align: justify;">Реализация действия через усиление активности локальных и регионарных нейротрансмиттерных систем - серотонин, норадреналин, гамма-аминомаслянная кислота. Односторонняя стимуляция эффективна в отношении обоих полушарий.</p>
 
  <p style="text-align: justify;">Основные эффекты: антиприступный, антиэпилептический, антиэпилептогенный, общее нейропротективное действие, иммуномодулирующий эффект.</p>
 
