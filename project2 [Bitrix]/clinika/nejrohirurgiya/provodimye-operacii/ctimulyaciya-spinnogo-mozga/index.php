    <h4 style="text-align: justify;"><font face="Times New Roman">КОМПОНЕНТЫ SCS-СИСТЕМЫ КОМПАНИИ MEDTRONIC</font></h4>
   
    <p style="text-align: justify;">Система для неиростимуляции состоит из трех базовых имплантируемых компонентов, которые могут использоваться унилатерально или билатерально:</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li style="text-align: justify;">Нейростимулятор</li>
     
      <li style="text-align: justify;">Удлиннитель</li>
     
      <li style="text-align: justify;">Чрезкожный цилиндрический или хирургический электрод</li>
     </ul>
   
    <p></p>
   
    <p style="text-align: justify;">Нейростимулятор, или имплантируемый генератор импульсов.</p>
   
    <p style="text-align: justify;">Нейростимулятор представляет собой изолированное устройство, похожее на кардиостимулятор, состоящее из батареи и электроники. Он имплантируется подкожно, и вырабатывает электрические импульсы, необходимые для унилатеральной или билатеральной стимуляции. Эти импульсы проводятся по удлиннителям и электродам в спинной мозг.</p>
   
    <p style="text-align: justify;">Удлиннитель</p>
   
    <p style="text-align: justify;">Удлиннитель - это тонкий изолированный провод. Удлиннители имплантируют подкожно, соединяя электрод с нейростимулятором.</p>
   
    <p style="text-align: justify;">Электроды</p>
   
    <p style="text-align: justify;">Электрод представляет собой тонкий изолированный четырехжильный провод с четырьмя, восемью или 16-ю контактами на кончике. Электрод имплантируется в эпидуральное пространство, чтобы проводить туда электрический ток, осущевляющий нейростимуляцию. Оптимальное положение электрода всегда находится в корреляции с зоной боли пациента. Для стимуляции спинного мозга электроды имплантируются в эпидуральное пространство (между позвонком с твердой мозговой оболочкой), при этом контакты электродов находятся достаточно близко к заднему рогу спинного мозга, чтобы осуществлять стимуляцию толстых миелиновых нервных волокон.</p>
   
    <p style="text-align: justify;">Неимплантируемые компоненты системы</p>
   
    <p style="text-align: justify;">К неимплантируемым компонентам системы относятся пульт пациента и программатор врача, которые используются для управления нейростимулятором.</p>
   
    <p style="text-align: justify;">Программатор врача N'Vision. Используется для программирования нейростимуляторов. Параметры импульсов, вырабатываемых нейростимулятором, могут быть неинвазивно изменены при помощи программатора врача. Программатор передает настройки нейростимулятору дистанционно при помощи радиочастотной связи.</p>
   
    <p style="text-align: justify;">Пульт пациента. Пульт пациента-это портативное устройство, которое позволяет пациенту самостоятельно включать и выключать нейростимулятор,когда это необходимо, а также проверять уровень заряда батареи нейростимулятора.</p>
   
