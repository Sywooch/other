    <h4 style="text-align: justify;"><font face="Times New Roman">CТИМУЛЯЦИЯ СПИННОГО МОЗГА - (SPINAL CORD STIMULATION - SCS)</font></h4>
   
    <p style="text-align: justify;">Метод лечения хронического болевого синдрома, спастики, нарушения функции тазовых органов.</p>
   
    <p style="text-align: justify;">Эффект достигается при помощи электрических импульсов, которые доставляются электродами, имплантированными в эпидуральное пространство.</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li style="text-align: justify;">Электрические импульсы активируют тормозящие боль нейроны в заднем роге спинного мозга, в результате чего чувство боли уменьшается;</li>
     
      <li style="text-align: justify;">Стимуляция также вызывает парестезию (ощущение мягкой приятной вибрации) в зоне, откуда происходит боль, и ее округе;</li>
     
      <li style="text-align: justify;">Применение SCS считается успешным, если удалось достичь более чем 50%-ного уменьшения боли по визульно-аналоговой шкале;</li>
     
      <li style="text-align: justify;">Однако 30%-ное уменьшение боли также является клинически значимым</li>
     
      <li style="text-align: justify;">Ежегодно более 15 000 пациентам во всем мире имплантируются нейростимуляторы для лечения хронической невропатической боли;</li>
     </ul>
   
    <p></p>
   
    <p style="text-align: justify;">Электроды соединяются с нейростимулятором, который имплантируется подкожно.</p>
   
    <p></p>
   <center style="text-align: justify;"><img src="/content/clinika/nejrohirurgiya/provodimye-operacii/ctimulyaciya-spinnogo-mozga/01.png" style="border-color: rgb(0, 0, 0);" width="560" height="239"  /></center> 
    <p></p>
   
    <p style="text-align: justify;">На сегодняшний день консервативные методы лечения не всегда обеспечивают достаточное обезболивание.</p>
   
    <ul style="list-style: url(http://skoraya55.ru/images/box.png);"> 
      <li style="text-align: justify;">Фармакотерапия, как правило, является первым и главным методом лечения, однако;</li>
     
      <li style="text-align: justify;">Более 50% пациентов остаются недовольными фармакотерапией, независимо от применяемого лекарства.</li>
     
      <li style="text-align: justify;">Многие лекарства вызывают различные побочные эффекты.</li>
     </ul>
   
    <p></p>
   
    <p style="text-align: justify;">Невропатическая боль (НБ) может быть фармакорезистентной (лекарства не приносят желаемого облегчения или вызывают непереносимые побочные эффекты).</p>
   
    <p style="text-align: justify;">Нейростимуляция является альтернативным методом лечения пациентов с НБ, в случае если традиционные консервативные методы лечения не приносят желаемого результата.</p>
   
