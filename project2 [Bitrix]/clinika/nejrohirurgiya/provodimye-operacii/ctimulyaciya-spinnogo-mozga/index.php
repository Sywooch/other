    <h4 style="text-align: justify;"><font face="Times New Roman">ТЕСТОВАЯ НЕЙРОСТИМУЛЯЦИЯ (ТН)</font></h4>
   
    <p style="text-align: justify;">ТН предполагает имплантацию электрода, который является частью системы для проведения теста. Введение электрода осуществляется под местной анестезией. Благодаря ТН уже на операционном столе удается получить анальгетический (обезболивающий) эффект, а также с большей долей вероятности прогнозировать эффективность нейростимуляции еще до имплантации всей системы.</p>
   
    <p style="text-align: justify;">Тестовый период в течение 7-10 дней проводится в амбулаторных условиях, приближенных к повседневным, для лучшей оценки больным динамики болевого синдрома и влияния его на повседневную активность. В тестовом периоде проводится подбор оптимальных параметров электростимуляции.</p>
   
    <p style="text-align: justify;">Если по результатам ТН удалось достичь 50% - го снижения боли по Визуально-аналоговой шкале (шкала оценки интенсивности боли), то пациенту может быть имплантирована система нейростимуляции полностью. В случае же неудачи, встанет вопрос о переходе на следующую ступень.</p>
   
    <p style="text-align: justify;">Отдельно следует отметить, что при некоторых болевых синдромах, например при  травматическом отрыве корешков нервного сплетения пациентам производятся хирургические вмешательства, например DREZ-операция. При грыже межпозвоночного диска - соответствующее иссечение грыжи. А при онкологических болях - хордотомия.</p>
   
    <p style="text-align: justify;">Обязательное условие: Перед установкой нейростимулирующей системы проводится тестовая стимуляция (см. выше), при которая позволяет врачу убедиться в эффективности методики.</p>
   
